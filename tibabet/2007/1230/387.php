&nbsp; سەۋەبى:
&nbsp; بۇ كېسەللىك، جىنسىي يول كىرىش ئاغزىغا جايلاشقان دەھلىز چوڭ بېزى (دالانچە چوڭ بېزى دەپمۇ ئاتىلىدۇ) نىڭ يۇقۇملىنىش سەۋەبىدىن كېلىپ چىقىدۇ.
&nbsp; ئالامىتى:
&nbsp; ئۆتكۈر دەۋردە دەھلىز چوڭ بېزىنىڭ شىللىق پەردىلىرى ئىششىش، قىزىش، قىزىرىش، ئاغرىش، بەزىدە يىرىڭ چىقىش، يىرىڭ چىقالماي قالسا، مونەك شەكىللىك يىرىڭلىق ئىششىق شەكىللىنىش قاتارلىق ئالامەتلەر؛ ئۆتكۈر دەۋرىدىن ئۆتۈپ كەتكەندىن كېيىن، خالتىلىق ئىششىق شەكىللىنىپ قايتا - قايتا قوزغىلىش، ھەرىكەت چەكلىمىگە ئۇچراش ۋە ئۇخلىيالماسلىقتەك ئالامەتلەر بىلەن ئىپادىلىنىدۇ.
&nbsp;  1 - رېتسېپ
&nbsp; تەركىبى:
&nbsp;  گاۋزىبان، بادرەنجى بۇيا، ئۈستقۇددۇس، سېرىق ئوت، قىزىلگۈل خەمىكى، شاھتەررە، بىستىپايەج، ئىزخىر، رۇمبەدىيان، تەرەنجىبىن، قارا ھېلىلە، گۈلى بىنەپشە، بەدىيان يىلتىزى پوستى ئالتە گرامدىن، چىلان 15 تال، سۇ مۇۋاپىق مىقداردا.
&nbsp; تەييارلاش ئۇسۇلى:
&nbsp;  دورىلارنى قاينىتىپ، تېرىپلىرىنى سۇزۇۋېتىپ&nbsp;  تەييارلىنىدۇ.
&nbsp; ئىشلىتىش مىقدارى:
&nbsp; كۈنىگە ئۈچ قېتىم، ھەر قېتىمدا 50 مىللىلىتىردىن ئىستېمال قىلىنىدۇ.
&nbsp;  2 -&nbsp; رېتسېپ
&nbsp; تەركىبى:
&nbsp;  چۆپچىنى، بىنەپشە، نېلۇپەر، موزا، قەلەمپۇر، جويۇز، قارا كۆز بەدىيان 15 گرامدىن، ئەنگۈر شىپا 30 گرام، مامرانچىنى، ئاقىرقەرھا، ئەمەن 20 گرامدىن.
&nbsp; تەييارلاش ئۇسۇلى:
&nbsp; دورىلارنى قاينىتىپ تەييارلىنىدۇ.
&nbsp; ئىشلىتىش مىقدارى:
&nbsp; كۈنىگە بىر قېتىم دورىدا ئولتۇرۇپ يەرلىك ئورۇن يۇيۇلىدۇ.
&nbsp;  3 - رېتسېپ
&nbsp; تەركىبى:
&nbsp;  مۇدەببەر كۇچۇلا 100 گرام، يىلان گۆشى 360 گرام، قىزىل سىر 60 گرام، چايان 20 گرام، ھەسەل ئۈچ ھەسسە.
&nbsp; تەييارلاش ئۇسۇلى:
&nbsp; دورىلاردىن يۇمشاق سوقۇپ، ئەلگەكتىن ئۆتكۈزۈپ، ئېرىتىلگەن ھەسەل بىلەن&nbsp; ئارىلاشتۇرۇلۇپ مەجۈن تەييارلىنىدۇ.
&nbsp; ئىشلىتىش مىقدارى:
&nbsp; كۈنىگە ئۈچ قېتىم، ھەر قېتىمدا 10 گرامدىن ئىستېمال قىلىنىدۇ.
