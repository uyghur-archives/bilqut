&nbsp;  رەيھان ئۇرۇقى ئالتە گرام،&nbsp; رەۋەن 12 گرام،&nbsp; كاسىنە ئۇرۇقى 60 گرام،&nbsp; سېرىق ئوت ئۇرۇقى ئالتە گرام،&nbsp; شېكەر 550 گرام،&nbsp; سېركە 15 گرام،&nbsp; سۇ ئىككى كىلوگرام.
&nbsp; تەييارلاش ئۇسۇلى:
