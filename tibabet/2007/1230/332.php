&nbsp; ئالامىتى:
&nbsp; كۆز ئېقى،&nbsp; شىللىق پەردە ۋە تېرە سارغىيىپ كېتىش،&nbsp; سۈيدۈك قىزىل چايدەك كېلىش،&nbsp; چوڭ تەرەت رەڭسىز ئاق كېلىش،&nbsp; تېرە قىچىشىش،&nbsp; ئېغىز ئاچچىق،&nbsp; تەشنالىق ئېغىر بولۇش،&nbsp; قۇرۇق ھۆ تۇتۇش ۋە جىگەر ئەتراپىدا ئېغىرلىق ھېس قىلىشتەك ئالامەتلەر بىلەن ئىپادىلىنىدۇ.
&nbsp;  1 -&nbsp; رېتسېپ
&nbsp; تەركىبى:
&nbsp;  قىزىلگۈل خەمىكى 35 گرام،&nbsp; كاسىنە 60 گرام،&nbsp; بەدىيان يىلتىزى 90 گرام،&nbsp; ئىزخىر 12 گرام،&nbsp; ئەفتىمۇن،&nbsp; كاسىنە ئۇرۇقى 30 گرامدىن،&nbsp; سۇ تۆت لىتىر،&nbsp; شېكەر 400 گرام.
&nbsp; تەييارلاش ئۇسۇلى:
&nbsp; دورىلارنى سۇدا قاينىتىپ،&nbsp; تىرىپلىرىنى ئېلىۋېتىپ،&nbsp; ئۈستىگە شېكەرنى سېلىپ تەييارلىنىدۇ.
&nbsp; ئىشلىتىش مىقدارى:
&nbsp; كۈنىگە ئۈچ قېتىم،&nbsp; ھەر قېتىمدا 10 مىللىلىتىردىن ئىستېمال قىلىنىدۇ.
&nbsp;  2 -&nbsp;  رېتسېپ
&nbsp; تەركىبى:
&nbsp;  سېرىق ھېلىلە پوستى 30 گرام،&nbsp; تەمرى ھىندى 60 گرام،&nbsp; ئەينۇلا 30 تال،&nbsp; سەرپىستان،&nbsp; بىنەپشە،&nbsp; سېرىق ئوت ئۇرۇقى،&nbsp; كاسىنە ئۇرۇقى 15 گرامدىن،&nbsp; پۇنۇس 75 گرام،&nbsp; تەرەنجىبىن 60 گرام.
&nbsp; تەييارلاش ئۇسۇلى:
&nbsp; دورىلارنى سۇغا بىر سوتكا چىلاپ قويۇپ،&nbsp; ئەتىسى قاينىتىپ، تىرىپلىرىنى سۈزۈۋېتىپ تەييارلىنىدۇ.
&nbsp; ئىشلىتىش مىقدارى:
&nbsp; كۈنىگە ئۈچ قېتىم،&nbsp; ھەر قېتىمدا 100 مىللىلىتىردىن ئىستېمال قىلىنىدۇ.
&nbsp;  3 - رېتسېپ
