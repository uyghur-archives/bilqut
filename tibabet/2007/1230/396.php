&nbsp; سەۋەبى:
&nbsp; بۇ كېسەللىك، ئومۇمىي بەدەننىڭ سوغۇقى ئېشىپ كېتىش ياكى نېرۋىنىڭ باشقۇرۇشى ئاجىزلىشىش سەۋەبىدىن كېلىپ چىقىدۇ.
&nbsp; ئالامىتى:
&nbsp; كېچىسى ئۇخلاۋېتىپ ئورنىغا سىيىپ قويۇش، بەزىدە ئويغاق ھالەتتىمۇ سۈيدۈكىنى تۇتالماي، سىيىپ سېلىش قاتارلىق ئالامەتلەر بىلەن ئىپادىلىنىدۇ.
&nbsp;  1 - رېتسېپ
&nbsp; تەركىبى:
&nbsp;  كۇندۇر، ئانارگۈلى، بەللۇت، يۇمغاقسۈت ئۇرۇقى 12 گرامدىن، زىرە، تاغ زىرىسى، جۇۋىنە ئالتە گرامدىن، ئامىلە، بەلىلە، سېرىق ھېلىلە، قارا ھېلىلە تۆت گرامدىن، سىيادان سەككىز گرام، ھەسەل ئۈچ ھەسسە.
&nbsp; تەييارلاش ئۇسۇلى:
&nbsp; دورىلارنى يۇمشاق سوقۇپ، ئەلگەكتىن ئۆتكۈزۈپ، ئېرىتىلگەن ھەسەل ئۈستىگە سېلىپ،&nbsp; ئارىلاشتۇرۇپ مەجۈن تەييارلىنىدۇ.
&nbsp; ئىشلىتىش مىقدارى:
&nbsp; كۈنىگە ئۈچ قېتىم، ھەر قېتىمدا بەش گرامدىن ئىستېمال قىلىنىدۇ.
&nbsp;  2 -&nbsp; رېتسېپ
&nbsp; تەركىبى:
&nbsp;  مۇرمەككى، كۇندۇر، ئاقاقىيا، شىيافى سامسا ئالتە گرامدىن، زەمچە توققۇز گرام، ئاق لەيلى ئۇرۇقى 21 گرام، ئاق زەنجىۋىل، زىغىر، كابۇل ھېلىلىسى پوستى 30 گرامدىن، ھەسەل ئىككى ھەسسە.
&nbsp; تەييارلاش ئۇسۇلى:
&nbsp; دورىلارنى يۇمشاق سوقۇپ، ئەلگەكتىن ئۆتكۈزۈپ، ھەسەل بىلەن ئارىلاشتۇرۇپ مەجۈن تەييارلىنىدۇ.
&nbsp; ئىشلىتىش مىقدارى:
&nbsp; كۈنىگە بىر قېتىم، ھەر قېتىمدا ئالتە گرامدىن ئىستېمال قىلىنىدۇ (بالىلارغا ئازايتىلىدۇ).
&nbsp;  3 - رېتسېپ
&nbsp; تەركىبى:
&nbsp;  چىگىت مېغىزى، ياڭاق مېغىزى مۇۋاپىق مىقداردا، تۇخۇم ئىككى دانە، سۇ ئىككى چىنە.
&nbsp; تەييارلاش ئۇسۇلى:
&nbsp; مېغىزلارنى تۇخۇم بىلەن سۇدا قاينىتىپ تەييارلىنىدۇ.
&nbsp; ئىشلىتىش مىقدارى:
&nbsp; كۈنىگە بىر قېتىم تەييارلاپ تۇخۇمىنى يەپ، سۈيى ئىچىلىدۇ.</span></div></td>
