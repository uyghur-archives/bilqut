<span id="manbilqut">
&nbsp; سەۋەبى:
&nbsp; بۇ كېسەللىك، كۆيدۈرگۈچى ئامىللار (ئوت، قىزىق ياغ، قايناق سۇ، كۈن نۇرى، توك، چاقماق، خىمىيىۋى ماددىلار) كۆيدۈرۈشى سەۋەبىدىن كېلىپ چىقىدۇ.
&nbsp; ئالامىتى:
&nbsp; ئېغىر - يەڭگىللىكىگە قاراپ ئۈچ دەرىجىگە بۆلۈنىدۇ. بىرىنچى دەرىجىلىك كۆيۈكتە تېرە قىزىرىپ ئىششىش؛ ئىككىنچى دەرىجىلىك كۆيۈكتە، تېرىدە سۇلۇق قاپارتما پەيدا بولۇش، بۇ ئىككىلىسىدە قاتتىق ئاغرىش؛ ئۈچىنچى دەرىجىلىك كۆيۈكتە، تېرە زەخىملىنىشى ئېغىر، ئەمما ئاغرىش سېزىمى يەڭگىل بولۇش ئالامىتى بىلەن ئىپادىلىنىدۇ.
&nbsp;  1 - رېتسېپ
&nbsp; تەركىبى:
&nbsp;  قۇرۇتۇلغان ھەمىشىباھار يوپۇرمىقى 50 گرام، زەيتۇن يېغى مۇۋاپىق مىقداردا.
&nbsp; تەييارلاش ئۇسۇلى:
&nbsp; ھەمىشىباھار&nbsp; يوپۇرمىقىنى يۇمشاق سوقۇپ، گۈل يېغى بىلەن ئارىلاشتۇرۇپ مەلھەم تەييارلىنىدۇ.
&nbsp; ئىشلىتىش مىقدارى:
&nbsp; كۈنىگە 2 ~ 3 قېتىم كۆيگەن ئورۇنغا چېپىلىدۇ.
&nbsp;  2 -&nbsp; رېتسېپ
&nbsp; تەركىبى:
&nbsp; &nbsp; ئاق لەيلىگۈلنىڭ گۈلى 30 گرام، توخۇ يېغى مۇۋاپىق مىقداردا.
&nbsp; تەييارلاش ئۇسۇلى:
&nbsp; ئاق لەيلىگۈلنى يۇمشاق ئېزىپ، توخۇ يېغى بىلەن ئارىلاشتۇرۇپ مەلھەم تەييارلىنىدۇ.
&nbsp; ئىشلىتىش مىقدارى:
&nbsp; كۈنىگە 2 ~ 3 قېتىم يەرلىك ئورۇنغا چېپىلىدۇ.
&nbsp;  3 - رېتسېپ
&nbsp; تەركىبى:
&nbsp;  قۇرۇتۇلغان ئاچچىق ئانار يىلتىزى 50 گرام، ھەسەل مۇۋاپىق مىقداردا.
&nbsp; تەييارلاش ئۇسۇلى:
&nbsp; ئانار يىلتىزىنى يۇمشاق سوقۇپ، ھەسەل بىلەن ئارىلاشتۇرۇپ مەلھەم تەييارلىنىدۇ.
&nbsp; ئىشلىتىش مىقدارى:
&nbsp; كۈنىگە 3 ~ 4 قېتىم يەرلىك ئورۇنغا چېپىلىدۇ.</span></div></td>
