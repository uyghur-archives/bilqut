<span id="manbilqut">
 بۇ، ئۆپكىنىڭ ياللۇغلىنىشلىق زەھەرلىنىشىدىن ئىبارەت بولىدۇ.
 سەۋەبى:&nbsp;&nbsp;مېڭىدىن ئىسسىق ياكى سوغۇق تەبىئەتلىك سۇيۇقلۇقلارنىڭ ياكى زۇكام، نەزلە ماددىسى بىلەن تۈرلۈك زەھەرلىك سۇيۇقلۇقلارنىڭ ئۆپكىگە قۇيۇلۇشى؛ بوغما ياكى گال ئەتراپىدىكى باشقا جاراھەتلەرنىڭ يىرىڭلىق ئاجرالمىلىرىنىڭ ئۆپكىگە چۈشۈشى؛ ئۆپكىنى ئوراپ تۇرغۇچى پەردىنىڭ ياللۇغى )زاتىلجەنبە( ياكى )كىرگىت گۆش( پەردىنىڭ ياللۇغلىنىشلىرىدا يىرىڭ ۋە ئىششىق ئۆپكىگە ئۆتۈش؛ ئۆپكىنىڭ ئۆزىدە سۇ يىغىلىپ، ۋاقىت ئۆتكەنسېرى بۇزۇلۇپ، ئۆپكىدە ياللۇغلىنىش ھاسىل قىلىش قاتارلىقلاردىن ئىبارەتتۇر.ئومۇملاشتۇرۇپ ئېيتقاندا، ئۆپكە ياللۇغىنىڭ مۇھىم سەۋەبى قان ۋە ئارتۇقچە بەلغەم بولۇپ، سەپرا تۈپەيلىدىن كەمرەك كېلىپ چىقىدۇ. كۆپىنچە نەزلىدىن كېيىن قوشۇلۇپ كېلىدۇ. ئۆپكىنىڭ زەھەرلىنىشى يۈرەككە تەسىر قىلىدۇ. ئۆپكىدىكى خىلىتلارنىڭ تارقىلىشى قىيىن بىر مەسىلە. ئۆپكە ياللۇغى سەۋەبلىرىگە قاراپ تۆۋەندىكىچە ئۈچ چوڭ تۈرگە بۆلۈنىدۇ:
1. ئىسسىقتىن بولغان ئۆپكە ياللۇغى.
ئالامىتى:&nbsp;&nbsp;قىزىتمىسىنىڭ داۋاملىق يۇقىرى بولۇشى بىلەن ئاغرىق كۆكرەك ساھەسىدە ئېغىرلىق، كېيىنچە قاتتىق ئاغرىش سېزىدۇ. يۈز ۋە كۆزلىرى قىزارغان، بولۇپمۇ ئىككى ئېڭەك قىسمى روشەن قىزارغان ھالدا بولىدۇ. ئېغىر كېسەللەرنىڭ قاپاقلىرى ئىششىغاندەك كۆرۈنىدۇ. تەشنالىق كۆپ بولۇپ، تىلى قېلىن گەز باغلايدۇ. تومۇرى دولقۇنسىمان سالىدۇ. بىمارنى يۆتەل بەكمۇ ئازابلايدۇ، سوغۇقتىن راھەتلىنىدۇ، كۆڭلى سوغۇق نەرسىلەرنى تارتىدۇ. ئەگەر ياللۇغلىنىش ئۆپكىنىڭ تۆۋەنكى قىسمىدا بولسا نەپەس قىيىنلىشىش ئېغىر بولىدۇ. يەنە بۇلاردىن باشقا، خىلىتلارنىڭ ئالاھىدىلىكلىرىگە ئاساسەن تۈرلۈك ئالامەتلەرمۇ قوشۇلۇپ كېلىدۇ. ئەگەر ياللۇغلىنىش ئۆپكىنىڭ يۇقىرىقى قىسمىدا بولسا، كېسەل كۆكرەك ئارقا قىسمى، تاغاق ئارىلىقىنىڭ ئاغرىشىدىن زارلىنىدۇ.
ئۆپكىدىكى ياللۇغ ئومۇمەن تۆۋەندىكى ئۈچ خىل ئەھۋال بىلەن ئۆزگىرىدۇ:
1( ماددا پىشىدۇ ۋە بەلغەم ئارقىلىق سىرتقا چىقىپ كېتىدۇ. بەلغەم ئاز ۋە ئۇنىڭ رەڭگى ۋە سۈپىتى سەۋەبچى ماددىلارنىڭ ئالاھىدىلىكلىرىگە ئىگە بولۇپ، نەتىجىدە ياللۇغلىنىشتىن قايتىپ، باشقا بەلگىلىرى يەڭگىللىشىپ، بەلغەم&nbsp;&nbsp;ئاسان چىقىدىغان بولىدۇ.
2( يىرىڭ يىغىپ ئەھۋال ئېغىرلىشىدۇ، ياللۇغلانغان جاي تېشىلىدۇ. شۇنىڭ بىلەن ئېغىزىدىن بېلىق پۇرىقى كېلىپ تۇرىدۇ. ئەگەر ياخشىلىق بولسا يىرىڭ بەلغەم بىلەن سىرتقا چىقىپ كېتىپ، ئۆپكە ماددىلاردىن تازىلىنىدۇ. ئۇنداق بولمىغاندا يىرىڭ باشقا ئەزالارغا تارقاپ كېتىدۇ. نەتىجىدە باشقا كېسەللىك قوشۇلىدۇ. يىرىڭ يىغىلىشقا باشلىغاندا كېسەلنىڭ ئەھۋالى بارغانسېرى ئېغىرلىشىپ، ماددا پىشقاندا قىزىتما پەسىيىدۇ، ئاغرىشمۇ بولمايدۇ. لېكىن كۆكرەك قىسمىدا ئېغىرلىق ھېس قىلىدۇ. ئەگەر تېشىلسە يەنە ھارارەت قايتىدىن باشلىنىپ، ئاغرىق تىترەيدۇ.
3( ماددىلارنىڭ سۇيۇق قىسمى تارقاپ كېتىپ، قويۇق قىسمى قېتىپ قالىدۇ. ئۇزۇن مۇددەت ساقلىنىدۇ. بۇ چاغدا باشقا ئالامەتلىرى يېنىكلەشكەندەك سېزىلىدۇ. ئەمما نەپەس قىيىنلىشىش ئەۋج ئېلىپ، قۇرۇق يۆتەل ۋە كۆكرەكتە ئېغىرلىق ساقلىنىپ قالىدۇ. بۇنىڭ تېشىلىشى ئاز ئۇچرايدۇ. بۇنداقلارنى داۋالاشتا ئۆپكىدىكى بۇ خىل قالدۇقنى چىقىرىشقا دىققەت قىلىش لازىم.
داۋاسى:&nbsp;&nbsp;قان ئېلىش ۋە تەبىئەتنى يۇمشاتقۇچى سۈرگىلەرنى بېرىش، ھۆقنە قىلىش لازىم.
قان ئېلىشتا ئەگەر كېسەللىك نەزلىدىن بولسا قىيپال تومۇرىدىن، قاندىن بولسا سافىن، كېيىن باسلىقتىن ئېلىنىدۇ. قان ئالىدىغان تومۇرلارنى تاللاشتا تۆۋەندىكى قائىدە بويىچە تاللاش كېرەك. )1( كېسەللىكنىڭ دەسلەپكى ئۈچىنچى كۈنلىرى قان ئالغاندا زەھەرلەنگەن ئۆپكىنىڭ قارشى تەرىپىدىكى تومۇرلاردىن ئېلىنىدۇ. مەسىلەن: ئوڭ تەرەپ ئۆپكە ياللۇغلانغاندا سول تەرەپتىن ئېلىش كېرەك. تۆتىنچى كۈنىدىن كېيىن قان ئېلىشقا توغرا كەلگەندە زەھەرلەنگەن ئۆپكە تەرىپىدىن ئېلىنىدۇ. ئۆپكىنىڭ قايسى تەرەپتىن زەھەرلەنگەنلىكىنى بىلىش ئۈچۈن شۇ تەرەپ كۆكرەكنىڭ ئېغىرلىق سېزىشى، شۇ تەرەپ يۈز قىسىمىنىڭ قىزىرىپ تۇرۇشى، ئاغرىق ياتقاندا شۇ تەرەپ ئېغىز بۇرجىكىدىن شۆلگەي ئېقىپ تۇرۇشلار ئاساس قىلىنىدۇ ياكى ئاغرىق قايسى تەرەپنى بېسىپ ياتقاندا بەلغەم كۆپ چىقسا، ياللۇغلىنىش ئۆپكىنىڭ شۇ تەرىپىدە ھەم بېسىپ كۆرگەندە ئاغرىق كۈچەيگەن تەرىپىدە ياللۇغلىنىش بولىدۇ.
