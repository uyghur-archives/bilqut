 مەئدىنىڭ ئاغرىشى مەئدىنىڭ مىزاج بۇزۇلۇشلۇق زەھەرلىنىشلىرى بىلەنمۇ پەيدا بولىدۇ. بۇ ھەقتە بىرىنچى پاراگرافتا سۆزلەپ ئۆتۈلدى. تۆۋەندە ئاشقازاننىڭ ياللۇغلىنىش )ۋەرەم(، جاراھەتلەر سەۋەبىدىن، يەل سەۋەبىدىن، يېمەكلىكلەرنىڭ سۈپىتى ۋە ئاشقازان ئەسەب )نېرۋا( لىرى سەۋەبلىك ئاغرىشىنى سۆزلەپ ئۆتىمىز. ئاشقازان ياللۇغى ۋە جاراھەتلىرىدىكى ئاغرىش كېسەللىك ئالامىتى بولغانلىقى ئۈچۈن ئالاھىدە پاراگرافتا سۆزلىنىدۇ.

 1. يەلدىن بولغان ئاشقازان ئاغرىقى
 ئاشقازان ئىچىدە قويۇق يەل كۆپ بولغاندا يەل ئاشقازان ئىچىگە سىغماي، زەئىپ ئاشقازان پەردىلىرىدە تارتىشىش، قورۇلۇش ئەھۋالى كېلىپ چىقىپ، ئاشقازان ئاغرىيدۇ.
 ئالامىتى:&nbsp;&nbsp;كۆپ كېكىرىدۇ، بەزىدە ھېق تۇتىدۇ. قورساق كىرگىت گۆشلىرى تارتىشىشقا باشلايدۇ. تاماقتىن كېيىن سول تەرەپ قورساق تال ئەتراپىنى تۇتۇپ كۆرگەندە قولغا بەلنىڭ ھەرىكىتى )غولدۇرلاش( سېزىلىدۇ. ئاغرىش كۆپىنچە سول تەرەپتىن باشلىنىدۇ.
 داۋاسى: كېپەك، تېرىق، قوناق، تۇزلارنىڭ ئۆزىنى يالغۇز ياكى ھەممىنى بىرلەشتۈرۈپ قىزىتىپ، ئىسسىق ھالدا ئاشقازان ئۈستىگە قويۇلىدۇ. يېيىش ئۈچۈن جاۋارىش كۇمۇنى )تەركىبى: زىرە &gt;بىر كېچە سىركىگە چىلاپ قۇرۇتۇلغان&lt; 594 گرام، قارىمۇچ 99 گرام، زەنجىۋىل، يالپۇز 132 گرامدىن، بۆرە ئەرمىنى 33 گرام، سەلىخە، دارچىن، ھەببى بىلسان، قىرفە، سۇمبۇل ۋە مەستىكى رۇمىدىن 13 گرامدىن ئېلىنىدۇ. زىرىنى مۇدەپپەر قىلىش ئۇسۇلى: زىرىنى توپا، ئەخلەتلىرىدىن ئايرىپ، قاچىغا سېلىپ، زىرە ئۈستىگە زىرىدىن تۆت ئىلىك يۇقىرى چىقىرىپ سىركە قۇيۇپ، زىرە بىر كېچە - كۈندۈز تۇرغۇزۇلغاندىن كېيىن سۈزۈۋېلىپ، سايە يەردە ئۈستىنى يېپىپ قۇرۇتىلغاندىن كېيىن بىر ئاز قىزىتىپ قورۇپ، چالىراق سوقۇلىدۇ. قالغان دورىلارنى يۇمشاق سوقۇپ، ئەلگەكتىن ئۆتكۈزۈپ، تالقان قىلىپ، ھەسەلنى ئېرىتىپ، توغرىغۇنى سېلىپ ئارىلاشتۇرغاندىن كېيىن، زىرە ۋە تالقان دورىلارنى ھەسەلگە سېلىپ تازا ئارىلاشتۇرۇپ، 3 ~ 7 گرامغىچە ئىستېمال قىلىنىدۇ. كۇندۇر، زىرە، پىننە قاتارلىقلارنى دائىم ئېغىزغا سېلىپ چايناش ۋە سۈيىنى يۇتۇش لازىم. لاچىندانىنى گۇلاب بىلەن قاينىتىپ بېرىلىدۇ. بەلغەم ماددىسىنى تازىلىغاندىن كېيىن ھەزىمنى ياخشىلايدىغان دورىلار بېرىلسە ياخشى نەتىجە بېرىدۇ.
ئەبۇ ئەلى ئىبىن سىنا ئۆز تەجرىبىسىدە بۇ خىل ئاشقازان ئاغرىقىنى كىندىكنىڭ ئوڭ - سول تەرىپىگە ھىجامەت قويۇش بىلەن داۋالىغاندا دەرھال ئاغرىق پەسەيگەن. سىركەنجىۋىل بىر ئاز قىزىتىپ بېرىلسە، ئاغرىقنى تېزلا توختىتىدۇ. ئاشقازان ئۈستىگە سىرتتىن ئىسسىق ياغلاردىن — مەستىكى يېغى، دارچىن يېغى، قۇستە يېغىدىن بىرىنى سۈرۈش لازىم. ئەگەر ئاشقازان يەل بىلەن تولغان بولسا ھۆقنە ئارقىلىق تازىلىغاندىن كېيىن، ھەببى سەكبىنەج )تەركىبى: سەبرەسۇقۇتى، سەكبىنەج، مۇقەل، غارىقۇن باراۋەر مىقداردا ئېلىپ، دورىلارنى يۇمشاق سوقۇپ، ئەلگەكتىن ئۆتكۈزۈپ قايناق سۇ بىلەن خېمىر قىلىپ ھەب تەييارلاپ، سايىدا قۇرۇتۇپ تەييارلىنىدۇ( دىن ھەر كۈنى كەچقۇرۇن 3 ~ 5 گرامغىچە بىر قېتىم ئىستېمال قىلىنىدۇ. ھۆللۈك يېمەكلىكلەرنى ئاز بېرىش، غىزاسىغا دارچىن، زىرە، سامساق قوشۇپ بېرىش كېرەك.
بەزىدە ئاغرىشنىڭ سەۋەبى شور بەلغەم بولغانلىقى ئۈچۈن، سوغۇق نەرسىلەرنى بەرگەندە، ئاغرىش پەسىيىش كۆرۈلىدۇ. بۇ چاغدا ماددا ئىسسىق ئىكەن دەپ خاتا گۇمان تۇغۇلىدۇ ياكى ئىسسىق نەرسىلەرنى بېرىش بىلەن ئاغرىق پەسىيىپ، ماددىنىڭ سوغۇقلۇقىغا گۇمان تۇغۇلىدۇ. مۇنداق ۋاقىتلاردا ئەتراپلىق ھالدا دورا بەلگىلەش لازىم.
تاماقتىن كېيىنلا ئاغرىش بولسا، قەي قىلىش ياخشى. ئەگەر ئاغرىش تاماقنى كۆپ يېيىش بىلەن مۇناسىۋەتلىك بولسا، بىر قانچە كۈن تاماق مىقدارىنى ئاز - ئازدىن بىر قانچە ۋاقىت بېرىش لازىم. بۇ خىلدىكى ئاغرىقلارغا تاماق بەرگەندە ئاشقازاننىڭ مىزاج ئەھۋالىغا قاراپ بەلگىلەش ناھايىتى مۇھىم. مەئدىنى قۇۋۋەتلەندۈرۈش ئۈچۈن جاۋارىش جالىنۇس نۇشدارى )تەركىبى: سۇمبۇل، قەلەمپۇر، قېلىن دارچىن، دارچىن، قاقىلە، خولىنجان، زەنجىۋىل، زەپەر، ئاقمۇچ، پىلپىل، دەريا قۇستى، سەئدى ھىندى، ئۇد بىلسان، ھەببۇلئاس، ئاسارۇن، قەسبۇزەرىرە يەتتە گرامدىن، مەستىكى 33 گرام، شېكەر 145 گرام، ھەسەلدىن &gt;كۆپۈكىنى ئالغان&lt; 145 گرامدىن ئېلىپ، زەپەردىن باشقا دورىلارنى يۇمشاق سوقۇپ، ئەلگەكتىن ئۆتكۈزۈپ، شېكەرنى قىيام قىلىپ ھەسەلگە قوشۇپ، سوقۇلغان زەپەرنى ئۇنىڭغا قوشقاندىن كېيىن سوقۇپ دورىنى ئۈستىگە سېلىپ ياخشى ئارىلاشتۇرۇپ، ئون كۈن تۇرغۇزۇلىدۇ( دىن كۈنىگە بىر قېتىم يەتتە گرامدىن ئىستېمال قىلىنىدۇ.
نۇشدارى لۆئلۈۋى )تەركىبى:زەرنەپ، دارچىن، ئايلىشى، سۇمبۇل، جويۇز، بەسباسە، قەلەمپۇر، دۇرنەج ئەقرەبى، يۇيۇلغان گىلى ئەرمىنى، گىلى مەختۇم، مەستىكى، زەپەر، كۈمۈش ۋەرەق، ئۇرۇقسىز زىرىق، ئىپار، تاباشىر ھەرقايسىسى ئۈچ گرامدىن، قىزىلگۈل، سەئدى كوفى، گۇلابقا سۈرۈلگەن ئاق سەندەل، ئاقلانغان سېمىز ئوت ئۇرۇقى &gt;تۇخۇمى خىرفە&lt; 33 گرامدىن، تېشىلمىگەن مەرۋايىت بەش گرام، ساپ ئامىلە 99 گرام، قىزىلگۈل گۇلابى، بېھى سۈيى، يازلىق ئالما سۈيى، تاتلىق ئامۇت سۈيى، بىدمىشكى گۇلابى، ئاق چوكا ناۋات، ھەسەل، ئاق قەنت ھەرقايسىسىدىن 396 گرامدىن ئېلىپ، ئاۋۋال ئامىلىنى بىر كېچە سۈتكە چىلاپ، ئەتىسى سۈزۈك سۇ بىلەن يۇيۇپ تازىلاپ، بىر كىلوگرام سۇ قۇيۇپ تاكى سۈيى تۈگەپ ئامىلە قالغۇچە قاينىتىپ، ئاندىن كېيىن سىم ئەلگەكتىن ئۆتكۈزۈپ، ئۈستىگە مېۋىلەر سۈيى، ناۋات، قەنت، ھەسەللەرنى سېلىپ قىيام قىلىپ، قالغان دورىلارنى يۇمشاق سوقۇپ، ئەلگەكتىن ئۆتكۈزۈپ، قىيام سوۋۇغاندىن كېيىن ئارىلاشتۇرۇپ، 40 كۈن قاچىدا ساقلىنىدۇ( دىن ھەر كۈنى ئەتىگەن - كەچتە تاماقتىن كېيىن 7 ~ 10 گرامغىچە بېرىلىدۇ. پۇرچاق جىنسلىق دانلار )ماش، لوبىيا، سېرىق پۇرچاق(، يەللىك چاي )قارا چاي( لار ئاشقازان ئىچىدە يەل پەيدا قىلىدۇ. بەك ھۆللۈك نەرسىلەر مەسىلەن: ئالما، قوغۇن، تاۋۇز، قېتىق، ئارپا نېنىمۇ زەرەر قىلىدۇ. شۇنىڭ ئۈچۈن، بۇلارنى كۆپ ئىستېمال قىلىشتىن ساقلىنىش لازىم.
