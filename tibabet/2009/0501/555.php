<span id="manbilqut">قان تۈكۈرۈش
 قان تۈكۈرۈش ئومۇمەن قاننىڭ چىقىش ئورنىغا قاراپ تۆۋەندىكىچە ئۈچ قىسىمغا بۆلۈنىدۇ:
1. ئېغىز بوشلۇقىدىن كەلگەن قان: چىش مىلىكلىرىدىن، تاماق ۋە يۇتقۇچتىن چىققان قانلار بولۇپ، چىش مىلىكلىرىنىڭ زەھەرلىنىشى، چىش تۈۋىنىڭ بوشاپ قېلىشى ياكى چىش مىلىك قان تومۇرلىرىنىڭ يېرىلىشىدىن تۈكۈرۈك بىلەن يېڭى قان چىقىدۇ. بۇنداق ھاللاردا چىش مىلىكىنىڭ زەھەرلىنىشلىرىگە قاراپ چىش كېسەللىكلىرىدە كۆرسىتىلگەن چارىلار قىلىنىدۇ. ھەببۇلئاس، ئانارگۈلى، رۇسۇت ۋە زەمچىلەرنى باراۋەر مىقداردا سوقۇپ، چىش تۈۋىگە قويۇلىدۇ.
2. يۇتقۇنچاقتىن كەلگەن قان: كاناي ئاغزىدىن چىققان قان بولۇپ، بۇنىڭ ئىپادىسى شۇكى، ئادەتتىكى بەلغەم ياكى تۈكۈرۈكلەردە قان بولماستىن، بەلكى بەلغەم چىقىرىش ئۈچۈن كۈچەپ قېقىلىش بىلەنلا ئاچ قىزىل رەڭلىك قان چىقىدۇ، يۆتەل بولمايدۇ.
داۋاسى:&nbsp;&nbsp;ھەببۇلئاس، ئانارگۈلى، ئانار پوستى، موزا ۋە زەمچىلەرنى باراۋەردىن ئېلىپ قاينىتىپ، ئۇنىڭ سۈيىدە گال چايقاشقا بۇيرۇلىدۇ ۋە قۇرسى كەھرىۋا )تەركىبى: كەھرىۋا، گىلى ئەرمىنى، سەمغى ئەرەبى، خۇنسىياۋشان، تاباشىر، نىشاستە، كەتىرا، ئاقاقىيا، ئانارگۈلى ھەرقايسىسىدىن تەڭ مىقداردا ئېلىپ، دورىلارنى يۇمشاق سوقۇپ، ئەلگەكتىن ئۆتكۈزۈپ، پاقىيوپۇرمىقى سۈيى ۋە گاۋزىبان سۈيى بىلەن خېمىر قىلىپ يۇغۇرۇپ، قۇرس &gt;توقىچاق&lt; تەييارلىنىدۇ( دىن كۈنىگە ئىككى قېتىم&nbsp;&nbsp;3 ~ 7 گرامغىچە ئىستېمال قىلىنىدۇ. ئەگەر يۇتقۇچتا جاراھەت بولسا كۇندۇر، خۇنسىياۋشاندىن يەتتە گرامدىن ئېلىپ سوقۇپ پۈركۈلىدۇ.
3. كانايدىن كەلگەن قان: يۆتەل ۋە كۆكرەك ساھەسىدە ئاغرىشلار بولۇپ، يۆتەل بىلەن كۆپۈكتەك قان چىقىدۇ، داۋاسى يۇقىرىقى بىلەن ئوخشاش.
4. دىماغدىن كەلگەن قان: بۇ خىل قان بۇرۇن يولى بىلەن چىقماستىن ئېغىزغا چۈشۈپ، تۈكۈرۈك بىلەن بىللە چىقىدۇ. كۆپىنچە بۇرۇن شىلىمشىقىنى ئىچىگە تارتىش بىلەن تولىراق ئۇيۇپ قالغان ھالەتتە چىقىدۇ. قان چۈشۈپ بولغاندىن كېيىن باش بىرئاز يېنىكلەشكەندەك سېزىلىدۇ. كۆپىنچە بىمار زۇكام بولغان ياكى بېشى ئاغرىغان بولىدۇ. بۇرۇننىڭ يۇقىرىقى تەرەپلىرى قىچىشىدۇ.
داۋاسى:&nbsp;&nbsp;قىيپال تومۇردىن قان ئېلىش ياكى كۈمۈش نەرسىلەر بىلەن ھىجامەت قويۇش لازىم. باشقا چارىلىرى بۇرۇن قاناشتىكى چارىلار بىلەن ئوخشاش.
5. ئۆپكە ۋە قىزىلئۆڭگەچتىن كەلگەن قان: يىقىلىش، سىرتقى زەربىلەر ياكى ئۆپكىنىڭ ساددا ۋە ماددىي زەھەرلىنىشلىرى ۋە ئۆپكە قان تومۇرلىرىنى زەخىملەندۈرگەن ئۆزگىرىشلەر نەتىجىسىدە يۆتەلدىن كېيىن بەلغەم بىلەن قان كېلىدۇ. قان دەسلەپتە ئاز بولسا كېيىنچە زەخىمنىڭ ئەھۋالىغا قاراپ كۆپىيىدۇ. رەڭگى قىزىلگۈل رەڭدە، دات رەڭدە بولۇپ، قاتتىق يۆتەل بىلەن ئۇيۇغان ھالدا بەلغەمگە ئارىلىشىپ چىقىدۇ. بۇ ئۆپكە ياللۇغى )زاتىلىرىيە( نىڭ ئالامىتى بولۇپ، كۆكرەك ۋە بېقىن ئەتراپلىرىمۇ ئاغرىيدۇ. يۆتەلگەنسېرى ئاغرىق كۈچىيىدۇ.
داۋاسى:&nbsp;&nbsp;قىيپال ياكى باسلىق تومۇرلىرىدىن قان ئېلىش، قۇرسى كەھرىۋا يەنى قان تۈكۈرۈش تابلېتى )تەركىبى: كەھرۋا، گىلى ئەرمىنى، سەمغى ئەرەبى، خۇنسىياۋشان، تاباشىر، نىشاستە، كەتىرا، ئاقاقىيا، ئانارگۈلىدىن تەڭ مىقداردا ئېلىپ، دورىلارنى يۇمشاق سوقۇپ، ئەلگەكتىن ئۆتكۈزۈپ، پاقىيوپۇرمىقى سۈيى ۋە گاۋزىبان سۈيى بىلەن خېمىر قىلىپ يۇغۇرۇپ، توقىچاق تەييارلىنىدۇ( دىن مۇۋاپىق مىقداردا، 3 ~ 7 گرامغىچە كۈنىگە ئىككى قېتىم ئىستېمال قىلىنىدۇ ۋە شەربىتى ئەنجىبار )تەركىبى: ئەنجىبار 22 گرام، ئاق سەندەل، قىزىل سەندەل )ئىكەكلەنگىنى( 16 گرامدىن، ئاقاقىيا يەتتە گرام، قەنتتىن 400 گرامدىن ئېلىپ، دورىلارنى چالا سوقۇپ، ئۈچ چوڭ چىنە قايناق سۇغا بىر كېچە چىلاپ، ئەتىسى سۈزۈپ، تىرىپىنى ئوبدان سىقىپ، سۈيىگە قەنت سېلىپ قىيام تەييارلىنىدۇ( نىڭ ئۆزىنى يالغۇز ياكى باشقا قان توختاتقۇچى دورىلار بىلەن بىللە ئۈچ قوشۇقتىن ئىستېمال قىلىنىدۇ.
قىزىلئۆڭگەچتىن كەلگەن قان دائىم قۇسۇق بىلەن كۆڭلى ئاينىشتىن كېيىن كېلىدۇ. قاننىڭ مىقدارى كۆپ بولسا ئاشقازان ياكى قىزىلئۆڭگەچتىن كەلگەنلىكىگە گۇمان قالمايدۇ. ئەمما ئاز بولغاندا باشقا جايدىن كەلگەن قاندىن پەرق قىلىش ئۈچۈن، يۆتەل بولماسلىقى ۋە ئاشقازاندىكى ئالامەتلەرگە دىققەت قىلىش كېرەك.</span></div></td>
