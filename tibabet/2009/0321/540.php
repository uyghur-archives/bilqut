<span id="manbilqut"> بۇ خىل كېسەلدە تاماق ماددىلىرى ھەزىم بولۇش ئالدىدا بەدەنگە تارقالماستىن، بەلكى ئىچ سۈرۈش بىلەن چىقىپ كېتىدۇ. بەزى تېۋىپلار بۇ كېسەلنى مەئدىنىڭ ئىسھالى دەپ ئاتىغان. يېگەن نەرسىلەر ئاشقازاندا ئۇزۇن تۇرماستىنلا تەرەت بىلەن چىقىپ كېتىدۇ. كۈنىگە نەچچە قېتىم يېگەن بولسا شۇنچە قېتىم ئىچى سۈرىدۇ. بۇ كېسەل سەۋەبلىرىگە قاراپ تۆۋەندىكىچە بىر قانچە تۈرلەرگە بۆلۈنىدۇ:
1( ئاشقازاننىڭ ماددىسىز مىزاجى بۇزۇلۇشى ياكى ماددىي سەۋەبلەردىن زەھەرلىنىشى: بۇنىڭدا ئاشقازان مىزاجىنىڭ بۇزۇلۇشى دېگەن پەسىلدە كۆرسىتىلگەن ئالامەتلەردىن باشقا ئىچ سۈرۈشمۇ بىللە قوشۇلۇپ كېلىدۇ.
داۋاسى: 
)1( ماددىسىز بولغان تۈرىدە ئاشقازاننى قىزىتىش ۋە نەملىكىنى قۇرۇتۇش تەدبىرلىرى قوللىنىپ مەجۈنى جاۋارىش كۇمۇنى )تەركىبى: زىرە &gt;بىر كېچە سىركىگە چىلاپ قۇرۇتۇلغان&lt; 594 گرام، قارىمۇچ 99 گرام، زەنجىۋىل، يالپۇز 132 گرامدىن، بۆرە ئەرمىنى &gt;توغرىغۇ&lt; 33 گرام، سەلىخە، دارچىن، ھەببى بىلسان، قىرفە، سۇمبۇل ۋە مەستىكى رۇمىدىن 13 گرامدىن ئېلىنىدۇ. زىرىنى مۇدەپپەر قىلىش ئۇسۇلى: زىرىنى توپا، ئەخلەتلىرىدىن ئايرىپ، قاچىغا سېلىپ، زىرە ئۈستىگە زىرىدىن تۆت ئىلىك يۇقىرى چىقىرىپ سىركە قۇيۇلىدۇ. بىر كېچە - كۈندۈز تۇرغۇزۇلغاندىن كېيىن سۈزۈۋېلىپ، سايە يەردە ئۈستىنى يېپىپ قۇرۇتۇپ، بىر ئاز قىزىتىپ قورۇلىدۇ ئاندىن زىرىنى چالىراق، قالغان دورىلارنى يۇمشاق سوقۇپ، ئەلگەكتىن ئۆتكۈزۈپ، تالقان قىلىنىدۇ. ھەسەلنى ئېرىتىپ، توغرىغۇنى سېلىپ، ئارىلاشتۇرغاندىن كېيىن زىرە ۋە تالقان دورىلارنى ھەسەلگە سېلىپ تازا ئارىلاشتۇرۇپ تەييارلىنىدۇ( دىن 3 ~ 7 گرامغىچە ئىستېمال قىلىنىدۇ.
جاۋارىش ئۇد Ⅰ )تەركىبى: ئۇد ھىندى، سۇمبۇل، مەستىكى، قەلەمپۇر، لاچىندانە، جۇزبۇۋائون گرامدىن، كابىل ھېلىلىسىنىڭ پوستى، كەرەپشە ئۇرۇقى، رۇم بەدىيان، تۇرۇنجى پوستى، بوزۇغا، بادرەنجى بۇيا بەش گرامدىن، زەپەر، بەسباسە، زەنجىۋىل ھەرقايسىسى 5.1 گرامدىن ئېلىپ، دورىلارنى سوقۇپ، 200 گرام قەنتنى قىيام قىلىپ ئارىلاشتۇرۇپ تەييارلىنىدۇ( دىن كۈنىگە يەتتە گرامدىن ئىستېمال قىلىنىدۇ. 93 ـ نۇسخىلار ۋە مەجۈنى پىلپىل بېرىلىدۇ.
)2( ماددىي سەۋەبتىن بولغان: ئەگەر بەلغەمدىن بولسا )كېسەلنىڭ چىرايى ئاق بولىدۇ، ئېغىزدىن سۇ ئاقىدۇ ۋە باشقا ئالامەتلەر بولىدۇ(، ئالدى بىلەن بىر نەچچە قېتىم كۆرسىتىپ ئۆتۈلگەن چارىلار بىلەن قەي قىلدۇرۇش، ئۇنىڭدىن كېيىن جاۋارىش كۇندۇر )تەركىبى: كۇندۇر، ئانارگۈلى 33رامدىن، قارىمۇچ، جۇۋىنە، سۇمبۇل، ھىڭ، رۇم بەدىيان، سىيادان ھەرقايسىسىدىن ئون گرامدىن ئېلىپ، دورىلارنى سوقۇپ، ئۈچ باراۋەر ھەسەلگە ئارىلاشتۇرۇپ، چىنە قاچىلاردا ساقلاپ تەييارلىنىدۇ( دىن مۇۋاپىق مىقداردا ئىستېمال قىلىنىدۇ.&nbsp;&nbsp;جاۋارىش جالىنۇس )تەركىبى: سۇمبۇل، قەلەمپۇر، قېلىن دارچىن، دارچىن، قاقىلە، خولىنجان، زەنجىۋىل، زەپەر، ئاقمۇچ، پىلپىل، دەريا قۇستى، سەئىدى ھىندى، ئۇد بىلسان،ھەببۇلئاس، ئاسارۇن، قەسبۇزەرىرە يەتتە گرامدىن، مەستىكى 33 گرام، شېكەر 145 گرام، ھەسەلدىن &gt;كۆپۈكىنى ئالغان&lt; 145 گرامدىن ئېلىپ، زەپەردىن باشقا دورىلارنى يۇمشاق سوقۇپ ئەلگەكتىن ئۆتكۈزۈپ، شېكەرنى قىيام قىلىپ چەككىلەنگەن ھەسەلگە ئارىلاشتۇرپ، ئۈستىگە تالقانلارنى سېلىپ، ئاخىرىدا سالايە قىلىنغان زەپەرنى ياخشى ئارىلاشتۇرۇپ تەييارلىنىپ، ئون كۈن تۇرغۇزۇلىدۇ( دىن كۈنىگە بىر قېتىم يەتتە گرامدىن ئىستېمال قىلىنىدۇ ياكى جاۋارىش خەرنوپ )تەركىبى: خەرنوپ نەبتى &gt;تازىلانغان&lt; سىركىگە چىلانغان زىرە، ھەببۇلئاس، پىستە، بەللۇت، قورۇلغان يۇمغاقسۈت ئۇرۇقى، مەستىكى ھەربىرىدىن 17 گرامدىن ئېلىپ، دورىلارنى سوقۇپ )ئەلگەكتىن ئۆتكۈزمەي(، ئۈچ باراۋەر ھەسەلنى بىر ئاز ئېرىتىپ، ئۈستىگە دورىلارنى سېلىپ، تازا ئارىلاشتۇرۇپ تەييارلىنىپ، چىنە قاچىلاردا ساقلىنىدۇ( دىن كۈنىگە 1 ~ 2 قېتىم 3 ~ 7 گرامغىچە ئىستېمال قىلىنىدۇ. سەپرادىن بولسا كۆپىنچە تەپ كېسەللىكلىرىدىن كېيىن يۇقىرىقىدەك ئىچ سۈرۈش ئالامەتلىرى كۆرۈنىدۇ. بەك ئىسسىقلىق تاماقلارنى يېيىش ياكى ھاراقنى كۆپ ئىچىش بىلەنمۇ كېلىپ چىقىدۇ. كېسەلدە ھارارەت ۋە تەرەتتە سەپرا بولىدۇ.
داۋاسى:&nbsp;&nbsp;ئانار شەربىتى ئىچىشكە بېرىلىدۇ. لازىم مىقداردىكى سېرىق ھېلىلە شېكەر بىلەن سوقۇپ بېرىلىدۇ. ئۇنىڭدىن كېيىن قۇرسى تاباشىر )تەركىبى: تاباشىر، بارىتەڭ، قىزىلگۈل ئۈچ گرامدىن، سەمغى ئەرەبى، كەتىرا، نىشاستە يەتتە گرامدىن، ئانارگۈلىدىن 1 ~ 3 گرامغىچە ئېلىپ، دورىلارنى يۇمشاق سوقۇپ، ئەلگەكتىن ئۆتكۈزۈپ، ئىسپىغۇل لوئابى بىلەن خېمىر قىلىپ، بىر پۇڭلۇق تىيىن چوڭلۇقىدا توقىچاق تەييارلاپ سايىدا قۇرۇتۇلىدۇ( دىن كۈندە بىر ياكى ئىككى قېتىم يەتتە گرامدىن&nbsp;&nbsp;ئىستېمال قىلىنىدۇ.
2( ئاشقازان يارىسى ۋە تۈرلۈك ئاشقازان جاراھەتلىرى سەۋەبىدىن پەيدا بولۇشى: كېسەلدە يارىنىڭ ئالامەتلىرى بىلەن )ئاشقازان يارىسىغا قاراڭ( ئىچى سۈرۈش بولىدۇ. غىزا يېيىش بىلەن قورساق ساھەسىدە )ئۈچەيدە( تولغاقسىمان ئاغرىش ۋە ئېچىشىش يۈز بېرىپ، ئىچى سۈرۈپ كېتىدۇ. بەزىدە قۇسىدۇ، ھارارەتمۇ بولىدۇ.
