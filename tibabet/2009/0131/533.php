&nbsp;&nbsp;يۇتقۇچ ۋە تاماق ئەتراپىنىڭ قويۇق بۇزۇق ماددىلار سەۋەبىدىن ئىششىپ، يالغان ئاق پەردە بىلەن قاپلىنىشى بوغما )خەنناق ۋابائى( دەپ ئاتىلىدۇ. بۇ كېسەل كۆپىنچە كىچىك بالىلاردا ئۇچرايدۇ. بوغما يۈرەككە تەسىر بەرسە ئاقىۋىتى يامان؛ ئۆپكىگە تەسىر بەرسە ئۆپكە ياللۇغىنى كەلتۈرۈپ چىقىرىدۇ. بوغما ئېغىر ئۆتكەندە قىزىتما، تارتىشىپ قېلىشلار قوشۇلۇپ كېلىشى مۇمكىن. بۇ ئاغرىق ئۈچۈن خەتەرلىك ئاقىۋەت بېرىدۇ. بوغمىنىڭ ماددىسى ئاساسەن قان، سەپرا ۋە بەلغەمدىن ئىبارەت. سەۋدادىن كەمرەك بولىدۇ.
&nbsp;&nbsp;ئالامىتى:&nbsp;&nbsp; بوغما )خەنناق ۋابائى( نىڭ ئالامەتلىرى ماددىنىڭ خىلىغا قاراپ تۆۋەندىكى بىر قانچە تۈرلۈك بولىدۇ:
1( ھارارەت يۇقىرى بولۇپ ئېچىشىش ئاغرىشقا قارىغاندا ئۆتكۈر بولىدۇ. گال ئىچىگە قارىسا بوغۇز ئەتراپى ئىششىغان بولۇپ، رەڭگى سارغۇچ ساختا پەردە بىلەن قاپلىنىپ تۇرىدۇ. بۇ سەپرانىڭ ئالامىتىدۇر.
2( ئاغرىش ناھايىتى قاتتىق بولۇپ، ئېچىشىدۇ، ھارارەت ئۈستۈن، گال، بوغۇز ئەتراپى قىزارغان بولۇپ، ئىششىق ئېنىق كۆرۈنىدۇ. بۇ قان ماددىسىنىڭ بەلگىسىدىن ئىبارەت.
3( بەلغەم ماددىسىدىن بولغاندا يۇقىرىقى ئالامەتلەر ئانچە ئېغىر بولمايدۇ. لېكىن ئىششىق ئاق رەڭلىك بولۇپ كۆرۈنىدۇ.
ئومۇمەن بوغما كېسىلىدە يۇقىرىقى ئالاھىدە ئالامەتلەردىن باشقا، كېسەلنىڭ ئومۇمىي ئالامەتلىرى — نەپەس سىقىلىش، ئېغىزنىڭ دائىم ئوچۇق قېلىشى، كۆزى چاچراپ چىقىپ كېتىدىغاندەك ھالەت ۋە تىلنىڭ سىرتقا ئېسىلغاندەك بولۇپ قېلىشى قاتارلىقلارمۇ كۆرۈلىدۇ. كېسەلنىڭ ئاۋازى پۈتۈپ قالىدۇ، كۆڭلى ئاينىيدۇ، لېكىن قۇسالمايدۇ.
&nbsp;&nbsp;داۋاسى:&nbsp;&nbsp; قان ياكى سەپرادىن بولسا، ئالدى بىلەن قان ئېلىش لازىم. بوغمىدا قان ئېلىش بەزىدە چوڭ نەتىجىلىك بولۇپ چىقىدۇ. ئەگەر كېسەلنىڭ ئومۇمىي ھالىتى ناچار بولسا، بىراقلا كۆپ قان ئېلىشقا بولمايدۇ. بىر قانچە كۈنگىچە كۈندە بىر قېتىم ئاز - ئازدىن قان ئېلىش لازىم ياكى پاچاق ساھەسىگە ھىجامەت قويۇلسىمۇ بولىدۇ. ئەگەر ياللۇغلىنىش ئېغىر بولماي ئاغرىق كۈچلۈك بولسا، گەدەننىڭ بىر تەرىپىگە ياكى ئىككى تەرىپىگە ھىجامەت قويۇلىدۇ. ھىجامەت قويغان جايدىن بىرئاز قان چىقىرىش مەنپەئەت قىلىدۇ. ئەگەر قان ئالمىسىمۇ بولىدىغان ھاللاردا قان ئالماستىن مۇۋاپىق سۈرگە بېرىش كۇپايە قىلىدۇ. ئەمما ئاغرىقنىڭ تىلى سىرتقا چىقىپ ئەھۋالى ناچارلاشقاندا، دەرھال تىل ئاستى تومۇرلىرىدىن بولسىمۇ قان چىقىرىش لازىم.
ئارقا تەرەتنىڭ ئەھۋالىغا دائىم دىققەت قىلىپ، قاتتىق كەلسە ياكى تەستە كەلسە يۇمشاتقۇچى ھۆقنە )تەركىبى: بۇغداي كېپىكى 33 گرام، سېرىقچېچەك ئۇرۇقى 17 گرام، ئىكلىلمىلىك 13 گرام، ئەنجۈر قېقى 23 گرام، تۇز 26 گرام قارا شېكەر 40 گرام، ئەپكامىدىن ئۈچ گرام ئېلىپ، قارا شېكەر، ئەپكامە، تۇزدىن باشقىلىرىنى قاينىتىپ سۈزۈپ، كېيىن دورىلارنى قوشۇپ، كلىزما ئۇسۇلى بىلەن ئارقا يولدىن بىر قېتىمدىلا كىرگۈزۈلىدۇ( قىلىنىدۇ ھەم تۈرلۈك مېۋە سۇلىرى بېرىلىدۇ. قان ۋە سەپرانىڭ بۇخاراتلىرىنى پەسەيتىش ئۈچۈن سىركە، گۇلاب، سىركەنجىۋىل، چىلان شەربىتى بېرىلىدۇ ۋە كاسىنە ئۇرۇقى، قاپاق ئۇرۇقى، يۇمغاقسۈت قاينىتىلغان سۇغا شاتۇت سۈيىنى قوشۇپ غار - غار قىلىشقا بېرىلىدۇ. غىزاسىغا ئاقلانغان ئارپا شىرىسى بېرىش پايدىلىق.
كېسەلنىڭ ئۈچىنچى كۈنى پۇنۇسنى كالا سۈتىدە ئېزىپ گالنى چايقاشقا بېرىلىدۇ ياكى يۇمغاقسۈت كۆكىنىڭ سۈيى، ئىتئۈزۈمىنىڭ سۈيى، شاتۇت، قىزىلگۈل، چۈچۈكبۇيىلارنى قايناتقان سۇ بىلەن بىلەن پۇنۇسنى ئېرىتىپ ئېغىزنى چايقايدۇ.
ياللۇغىدىن يىرىڭ پەيدا بولۇپ ئېغىز ئالماي ئاغرىتىپ تۇرسا توغرىغۇ، ھىڭ ۋە قارلىغاچ مايىقىنى ئوخشاش مىقداردا ئېلىپ، يېڭى كالا سۈتى ۋە قىزدۇرغان كۈنجۈت يېغى بىلەن گالنى غار - غار قىلسا ئاسان ئېغىز ئالىدۇ. ئېغىز ئالغاندىن كېيىن ئېغىز ئىچىنى تازىلاش ئۈچۈن كالا سۈتىگە ھەسەل ئارىلاشتۇرۇپ ئېغىز چايقىلىدۇ.
بوغمىنى ئېغىز ئالدۇرۇش ئۈچۈن، تاشقى تەرەپتىن سوغۇق دورىلارنى ئىشلىتىشكە رۇخسەت قىلىنمايدۇ. مۇنداق قىلىش ياللۇغلىنىشتىكى ئوفۇنەتلەرنى بەدەنگە تارقىتىپ قويىدۇ.
ئەگەر سەۋدانىڭ كۆپلۈكى كۆرۈلسە، يۇقىرىقى چارىلار بىلەن بىللە ئايارەج پەيقىرا )تەركىبى: ئۇدبىلسان، مەستىكى رۇمى، ئاسارۇن ۋە زەپەر ئون گرامدىن، سەبرە 19 گرام، ھەسەلدىن ئۈچ ھەسسە ئېلىپ، زەپەردىن باشقا دورىلارنى سوقۇپ، ئەلگەكتىن ئۆتكۈزۈپ، زەپەرنى ئايرىم سوقۇپ، بىر ئاز قىزىتىلغان ھەسەلگە ئارىلاشتۇرۇپ ئۈستىگە قالغان دورىنى قۇيۇپ خېمىردەك يۇغۇرۇپ، 40 كۈندن تۇرغۇزۇلىدۇ( دىن ھەر كۈنى بىر قېتىم ئون گرامدىن ئىستېمال قىلىنىدۇ. مەتبۇخى ئەفتىمۇن )تەركىبى: ئەفتىمۇن، كابىل ھېلىلىسى ۋە ئۈستقۇددۇس 33 گرامدىن، شاھتەررە، بىستىپايەج ۋە سانا 17 گرامدىن، غارىقۇن ئۈچ گرام، سەبرىدىن يەتتە گرامدىن ئېلىپ، ئەفتىمۇندىن باشقا دورىلارنى سوقۇپ، ئۈچ كىلوگرام سۇ قۇيۇپ يېرىمى قالغۇچە قاينىتىپ، ئەفتىمۇننى بىر خالتىغا سېلىپ، ئاغزىنى بېكىتىپ قازانغا تاشلاپ قويۇپ، دورىلارنى داكا بىلەن سۈزۈپ، سىقىپ تەييارلاپ سۈزۈلگەن دورا سۈيى ئۈستىگە سەبرە ۋە غارىقۇننى &gt;سىم ئەلگەكتىن ئۆتكۈزۈلگەن&lt; سېلىپ تەييارلىنىدۇ( قوشۇمچە بېرىلىدۇ.
