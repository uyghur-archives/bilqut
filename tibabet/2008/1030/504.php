<span id="manbilqut">
&lt;نوزۇلۇلما&gt;_ئەرەبچە سۇ چۈشۈش دېگەن سۆز بولۇپ، كۆرۈشكە توسالغۇچى بولغۇچى سۇيۇقلۇقلار نەتىجىسىدە كۆزنىڭ كۆرۈشى ئوخشاش بولمىغان دەرىجىدە بۇزۇلۇشتىن ئىبارەت. كۆزگە سۇ چۈشۈشنىڭ كېلىپ چىقىشى ۋە سەۋەبى جەھەتتە ئىختىلاپلار كۆپ بولسىمۇ، كۆپلىگەن تېۋىپلار ئەبۇ ئەلى ئىبىن سىنانىڭ تۆۋەندىكى پىكرىنى قۇۋۋەتلەيدۇ.
نۇزۇلۇلمائى — ئوقياسىمان قاتلام )ئەنەبىيە( تۆشۈكىدە ئاقسىلسىمان قاتلام بىلەن مۈڭگۈز قاتلام ئارىسىدا تۇرۇپ قالىدىغان كېرەكسىز سۇيۇقلۇق )رۇتۇبەتى غەرىبى( لاردىن ئىبارەت.
سۇيۇقلۇق چۈشۈش مىقدارى ۋە كەيپىيات جەھەتلىرىدىن ھەرخىل بولىدۇ. مىقدار جەھەتتىن سۇيۇقلۇق مەزكۇر تۆشۈككە قارىغاندا جىقراق بولۇپ، ئۇنى پۈتۈنلەي توسۇۋالىدۇ. بۇ ئەھۋالدا كۆز ھېچنەرسىنى كۆرەلمەي قالىدۇ. بەزىدە تۆشۈككە قارىغاندا ئاز بولىدۇ. ئۇنىڭ بىر تەرىپىنى توسۇپ، بىر تەرىپىنى ئوچۇق قويىدۇ؛ توسۇلغان تەرەپتە تۇرغان نەرسىلەرنى كۆز كۆرمەيدۇ. ئوچۇق قالغان تەرەپتىكىلەرنى كۆرۈش مۇمكىن. بەزىدە كۆز نەرسىلەرنىڭ يېرىمىنى ياكى بىر قىسمىنى كۆرۈپ قالغىنىنى كۆرمەيدۇ، لېكىن باشنى ئايلاندۇرسا كۆرەلەيدۇ. بەزىدە ئۇ نەرسىنى بىر قېتىم تولۇق كۆرۈپ، ئىككىنچى قېتىمدا تولۇق كۆرەلمەيدۇ. بۇ، نەرسىلەرنىڭ تۇرۇش جايىغا قاراپ شۇنداق بولىدۇ، چۈنكى نەرسە پۈتۈن گەۋدىسى بىلەن توسۇلغان جاينىڭ ئۇدۇلىدا بولسا، كۆز ئۇنىڭ ھېچبىر جايىنى كۆرمەيدۇ؛ ئوچۇق قالغان تەرەپتە بولغاندا پۈتۈن گەۋدىسىنى كۆرەلەيدۇ. سۇ تولۇق بولمىغان توسۇلۇش كۆرۈش تۆشۈكىنىڭ يۇقىرىقى تەرىپىدە بولۇپ، يۇقىرىنىلا توسۇيدۇ ياكى يۇقىرى ھەم تۆۋەندە بولىدۇ. بەزىدە تۆشۈكنىڭ دەل ئوتتۇرىسىدا بولۇپ، تۆشۈكنىڭ چۆرىسى ئوچۇق قالىدۇ. بۇ ھالدا نەرسىلەرنىڭ ئەتراپى كۆرۈنىدۇ. ئوتتۇرىسى كۆرۈنمەيدۇ. ھەتتا ئۇ نەرسىنىڭ ئوتتۇرىسىدا تۈڭلۈك ياكى چوڭقۇرغا ئوخشاش بىر شەكىل كۆرۈنۈپ تۇرىدۇ. يەنى ئۇ كىشى شۇ جايىنى كۆرمەيدۇ ۋە ئۇ يەردە بىر قاراڭغۇلۇق كۆرۈنۈپ تۇرۇپتۇ، دەپ گۇمان قىلىدۇ.
كۆزگە سۇ چۈشۈشنىڭ كەيپىيات جەھەتتىن ھەرخىل بولۇشىغا كەلسەك، ئۇنىڭ قويۇق - سۇيۇقلۇقى ھەرخىل بولىدۇ. بەزىسى سۇيۇق ھەم سۈزۈك بولۇپ، نۇر )چىراغ يورۇق( نى ۋە كۈننى توسۇمايدۇ؛ بەزىسى بەك قويۇق بولىدۇ. قويۇق خىللىرىدىن شۇنداقلىرى باركى، ئۇ ناھايىتى قاتتىق بولۇپ، سۇ چۈشۈش دائىرىسىدىن چىقىپ كېتىدۇ ۋە ئۇنى داۋالىغىلى بولمايدۇ. سۇيۇق ھەم سۈزۈك خىلدىكىسى داۋالاشنى ئاسان قوبۇل قىلىدۇ.
رەڭ جەھەتتىن ھەرخىل بولۇشى — بەزىسى ھاۋارەڭ، بەزىسى ئاق گەجگە ئوخشاش، بەزىسى ئاق مەرۋايىت رەڭلىك، بەزىسى ئاق بولۇپ، كۆك ياكى ئالتۇن رەڭگە مايىل كېلىدۇ. بەزىسى سېرىق، بەزىسى قارا، بەزىسى غۇۋا بولىدۇ. ئۇنىڭ گەج رەڭدىكىسى يېشىل، گۈڭگۈرت رەڭلىكلىرىنى، تۇم قارا ۋە سېرىقىنى داۋالاشمۇ ھەتتا كېسىپ ئېلىشمۇ مۇمكىن ئەمەس.

&nbsp; سەۋەبى:&nbsp;  
① يىقىلىش ياكى باشقا زەربە نەتىجىسىدە مېڭنىڭ سىلكىنىشى بىلەن مېڭىدىكى سۇيۇقلۇقنىڭ كۆز قاتلاملىرىغا چۈشۈپ توختاپ قېلىشى؛
② ناچار ئوزۇقلۇق ماددىلىرىنىڭ بۇسلىرى نەتىجىسىدە، خۇددى ئوت ھاسىل بولغاندەك تەدرىجىي ھالدا سۇيۇقلۇق كېلىپ كۆزگە چۈشۈپ قالىدۇ؛
③ ئۇزۇنغا سوزۇلغان قاتتىق باش ئاغرىش؛
④ كۈچ بىلەن قاتتىق قۇسۇش؛
⑤ قاتتىق سوغۇق مىزاجلىق ياكى تاسادىپىي ھالدا قارغا، مۇزدەك سوغۇق سۇغا چۆكۈپ كېتىشلەر؛
⑥ كۆزنىڭ زەئىپلىكى بولۇپمۇ ياشانغان كىشىلەر ياكى ئۇزۇن ۋاقىت كېسەل بولغۇچىلار كۆزىنىڭ زەئىپلىكى؛
&nbsp; ئالامىتى:&nbsp;  كۆزگە سۇ چۈشۈشنىڭ دەسلەپكى ئالامەتلىرى ئېنىق سەۋەبچىسى بولمىغان ھالدىكى كۆز ئالدىدا پاشا - كۇمۇتىدەك نەرسىلەر ئۇچۇپ يۈرگەندەك سېزىلىشتىن ئىبارەت. شۇنىڭ بىلەن بىللە، كۆزدە، بولۇپمۇ ئىككى كۆزنىڭ بىرىدە خىرەلىك بولىدۇ ۋە كۆزگە چىراغقا ئوخشاش يورۇقلۇق نەرسىلەر ئىككى بولۇپ كۆرۈنىدۇ.
كۆزگە سۇ چۈشۈش بىلەن كۆز ئىچكى قىسمىدىكى توسۇلۇشتىن كېلىپ چىققان كۆز ئالدىدا پاشا ئۇچقاندەك كۆرۈنۈش ئوتتۇرىسىدىكى پەرق تۆۋەندىكىچە:
1( ئەگەر كۆزگە سۇ چۈشۈش بولسا كۆزنىڭ بىرسىنى يۇمغاندا ئىككىنچى كۆزنىڭ قارىچۇقى كېڭىيىدۇ، كۆز ئىچكى قىسمىدىكى توسۇلۇشتا كېڭەيمەيدۇ.
2( كۆز ئالدىدا چىۋىن ئۇچقاندەك كۆرۈنۈش بىر كۆزدە كۆپرەك ئۇچرايدۇ.
3( كۆز ئالدىدا چىۋىن ئۇچقاندەك كۆرۈنۈش بارغانسېرى زىيادىلىشىدۇ. يەنى دەسلەپتە پاشىدەك، كېيىنچە چىۋىندەك نەرسىلەر كۆرۈنىدۇ.
4( مەئدىنىڭ كۆپكەن ياكى بوش بولۇشى چىۋىن ئۇچقاندك كۆرۈنۈشنىڭ ئاز - كۆپ بولۇشىغا سەۋەب بولمايدۇ.
5( چۈشكەن سۇنىڭ رەڭگىگە قاراپ ھەرخىل رەڭلىك زەھەرلىنىش كۆرۈنىدۇ.
6( سۈرگە ياكى ئايارەج دورىلار نەتىجە بەرمەيدۇ. تېۋىپ داۋالاشتىن بۇرۇن زادى كۆزگە سۇ چۈشۈشمۇ ياكى ئەمەسمۇ ئېنىق ئايرىشى كېرەك. قالايمىقان چارە - تەدبىر قىلىش بىلەن كۆزگە سۇ چۈشۈشنى داۋالاش دائىرىسىدىن چىقىپ ۋاقىتنى قولدىن بېرىپ قويۇشى مۇمكىن.
&nbsp; 
〓〓&nbsp; داۋاسى:&nbsp; كۆزگە سۇ چۈشۈش ئىكەنلىكى ئېنىقلىنىپ بولغاندىن كېيىن، ماددىنى پىشۇرۇش ئۈچۈن ھەببى زەھەپ )تەركىبى:سەبرە سۇقۇترى[ZW(]سەبرە سۇقۇتى — سەبرىنىڭ بىر خىل ياخشى تۈرى بولۇپ، سېرىق رەڭدە پارقىراپ تۇرىدۇ.[ZW)] 33 گرام، تۇربۇت 23 گرام، سېرىق ھېلىلە پوستى 17 گرام، سوقمۇنىيادىن 61 گرامدىن ئېلىپ، دورىلارنى يۇمشاق سوقۇپ، ئەلگەكتىن ئۆتكۈزۈپ، سۇ بىلەن قارىمۇچ چوڭلۇقىدا كۇمىلاچ تەييارلاپ، سايىدا قۇرۇتۇلىدۇ( دىن ھەر كۈنى ئاخشامدا يەتتە گرامدىن ئىستېمال قىلىنىدۇ. تازىلاپ چىقىرىش ئۈچۈن مائۇلئۇسۇل Ⅲ )تەركىبى: كەرەپشە يىلتىزى، بىخ بەدىيان يىلتىزى ۋە كەبىر 33 گرامدىن، رۇم بەدىيان ۋە كەرەپشە ئۇرۇقى يەتتە گرامدىن؛ غاپەس، ئەپسەنتىن ۋە بۇدۇشقاق 17 گرامدىن، قەنتەرىيوندىن ئون گرامدىن ئېلىپ، دورىلارنى چالا سوقۇپ، بىر كېچە سۇغا چىلاپ قويۇلىدۇ. ئەتىسى ئىككى كىلوگرام سۇ بىلەن قاينىتىپ، بىر كىلوگرام قالغاندا سۈزۈۋېلىپ تەييارلىنىدۇ( كە 132 گرام گۈلقەنت قوشۇلۇپ، كۈنىگە ئۈچ قېتىم، ھەر قېتىمدا بەش قوشۇقتىن ئىستېمال قىلىنىدۇ. ئەمما بۇ خىل دورىلارنى بەرگەندە ئاغرىقنىڭ ئومۇمىي ئەھۋالىنى نەزەرگە ئېلىش لازىم. سۈرگە دورىلارنى بەك كۈچلۈك ياكى ئارتۇق بەرگەندە زەرەر كەلتۈرۈشى مۇمكىن.
قان ئېلىش، بولۇپمۇ قۇلاق ئارقا تومۇرىدىن قان ئېلىشنى ئويلاپ بېقىش كېرەك. چۈشكۈرتكۈچى ۋە قەي قىلدۇرغۇچى دورىلارنىڭ پايدىلىق تەرەپلىرى بولسىمۇ لېكىن خەتەردىن خالىي ئەمەس.
ئەفئانىڭ زەھىرىنى كۆزگە ناھايىتى ئاز مىقداردا سۈرمىدەك تېمىتىپ، بۇ كېسەلنى داۋالىغانلىق ھەققىدە خاتىرىلەر بار.
بەزىلەر ماددىنى پىشۇرۇش ئۈچۈن مەتبۇخى قەنتەرىيون بېرىشنى كۆرسىتىدۇ )تەركىبى: قەنتەرىيون، تۇربۇت ئون گرامدىن، ئۇرۇقسىز ئۈزۈم 60 گرام، بىستىپايەجدىن 20 گرامدىن ئېلىپ، دورىلارنى قائىدە بويىچە مۇۋاپىق مىقداردىكى سۇغا سەككىز سائەت چىلاپ قاينىتىپ، تىرىپلىرىنى سۈزۈۋېتىپ تەييارلاپ، كۈندە ئىككى قېتىم 100 ~ 500 مىللىلىتىر، تاماقتىن كېيىن ئىستېمال قىلىنىدۇ.(
&nbsp; شەربىتى سەندەل )تەركىبى: ئاق سەندەل 80 گرام، قىزىلگۈل 160 گرام، قەنت 100 گرامدىن ئېلىپ، ئاق سەندەلنى چالا سوقۇپ، قىزىلگۈل بىلەن بىللە مۇۋاپىق مىقداردىكى سۇغا چىلاپ قاينىتىپ، تىرىپلىرىنى سۈزۈۋېتىپ، سۈيىدە قەنتنى قىيام قىلىپ تەييارلاپ، قان بېسىمنى&nbsp; چۈشۈرۈش، يۈرەك ۋە مېڭىنى قۇۋۋەتلەپ، يۇقىرى قان بېسىم، ئىسسىقتىن بولغان يۈرەك چالاڭغىراش قاتارلىقلارغا ئىشلىتىلىدۇ. كۈندە ئۈچ قېتىم، ھەر قېتىمدا 30 ~ 60 مىللىلىتىر، تاماقتىن كېيىن ئىستېمال قىلىنىدۇ(، مەتبۇخى قەنتەرىيونغا ئون گرام ئايارەج پەيقىرانى قوشۇپ بېرىلىدۇ. ئاغرىق جىنسىي ئالاقە ۋە مېڭىنى سۇسلاشتۇرىدىغان ئىشلاردىن ساقلىنىش لازىم.
ئەبۇ ئەلى ئىبىن سىنا — نەزۇل[ZW(]قەدەھ — 11 - ئەسىردىكى تېۋىپلارنىڭ كۆزگە سۇ چۈشۈشنى ئوپېراتسىيە قىلىش جابدۇقى مىقدەمە، كېسىۋېلىش ئۇسۇلى قەدەھ دەپ ئاتىلىدۇ.[ZW)] تامام بولغاندا كۆز پۈتۈنلەي كۆرمەيدۇ. بۇنىڭ دورىسى پەقەت كېسىۋېلىش )قەدەھ( تۇ — دېگەن. ھەقىقەتەنمۇ نۇزۇلۇلمانى باشلىنىشىدىلا بايقاپ تەدبىر قىلىنسا پايدا قىلىدۇ. ئەمما ئورنىشىپ قالغاندا كېسىپ ئېلىشتىن باشقا ئىلاج يوق. بۇ كېسەلگە دۇچار بولغان كىشىنىڭ بەك تويۇپ تاماق يېيىش ۋە ئىچىشتىن، جىنسىي ئالاقىدىن ساقلىنىشى، چۈشتە بىر قېتىم تاماق يېيىش بىلەن چەكلىنىش كېرەك؛ شۇنىڭدەك بېلىق، ھۆل مېۋە، بولۇپمۇ ھەزىم بولۇشى ئېغىرراق گۆشلەردىنمۇ ساقلىنىشى لازىم.
كۆزگە سۇ چۈشۈشنى كېسىۋېلىش تەجرىبىسى: ئالدى بىلەن كېسىۋېلىشقا مۇمكىن ياكى مۇمكىن ئەمەسلىكىنى ئېنىقلاش كېرەك )بۇلار يۇقىرىدا تەپسىلىي سۆزلەندى(، مەسىلەن: سۇيۇق ھەم سۈزۈك بولغىنىنى ئالساق، ئۇنى يورۇق چۈشۈپ تۇرىدىغان خالى جايدا تەكشۈرۈپ كۆرۈپ، ئۈستى بارماق بىلەن بېسىلىدۇ. ئۇ سۇ دەرھال تارقىلىپ قايتا يىغىلىدۇ. ئاشۇنداق كۆزگە چۈشكەن سۇنى كېسىۋالغاندا يوق بولۇپ كېتىشى مۇمكىن. ئەمما شۇنداق تەجرىبىنى بىر قانچە قېتىم تەكرارلاش كېسەلنى تىنچسىزلاندۇرىدۇ ۋە كېسىۋېلىشنى قىيىنلاشتۇرىدۇ. بەزىدە ئۇنى باشقا يول بىلەن سىناپ كۆرۈلىدۇ؛ كۆزگە بىر پارچە پاكىز پاختا قويۇپ شۇ پاختىغا قاتتىق پۈۋلىنىدۇ. ئاندىن يىغىلغان سۇدا قوزغىلىش بار - يوقلۇقىنى بايقاش ئۈچۈن پاختىنى تېزلىكتە ئېلىپ قارىلىدۇ. ئەگەر قوزغىلىش كۆرۈلسە بۇنى كېسىش نەتىجىلىك بولۇشى مۇمكىن. بىر كۆزنى يۇمغاندا ئىككىنچىسىنىڭ قارىچۇقى كېڭەيسىمۇ كېسىش نەتىجە بېرىدۇ.
كۆزگە سۇ چۈشۈشنى كېسىشتىن ئاۋۋال تۆۋەندىكى بىر قانچە تەدبىرلەرنى قوللىنىش كېرەك:
① پۈتۈن بەدەننى خۇسۇسەن مېڭىنى ماددا سۈرگىسى بىلەن تازىلاش ياكى قان ئېلىش لازىم.
② تاماقنى ئاز بېرىش، خۇسۇسەن ئېغىر تاماقلاردىن، ماددىنى قويۇقلاشتۇرغۇچى نەرسىلەردىن، مەسىلەن: كالا گۆشى، ماش، پۇرچاق، سامساق، پىدىگەن، شوخلا، تۇرۇپ، قېتىق، ھاراقلارنى چەكلەش، جىنسىي ئالاقىنى مەنئى قىلىش كېرەك. چۈنكى بۇ نەرسىلەر كۆزگە سۇ چۈشۈشنى تېزلەشتۈرىدۇ.
③ ئاغرىقتا باش ئاغرىقى، يۆتەل، زۇكام، يۈرەك سىقىلىش ۋە تېز غەزەپلىنىدىغان ئەھۋاللار بولسا داۋالاپ ساقايتىش لازىم. بۇلار كۆزدە ئىششىق پەيدا بولۇش ياكى كۆزگە سۇ چۈشۈشنى تەكرارلايدىغان ئامىللاردۇر.
④ كېسىش ئالدىدا ھاراقتىن، جىنسىي ئالاقىدىن ۋە مۇنچىغا چۈشۈشتىن ساقلىنىش لازىم.
⑤ &gt;كامالەتكە يەتكۈزۈش&lt; كېرەك. كېسەل باشلىنىشىدىلا كەسمەستىن بەلكى سۇ توختاپ، ئۇنىڭ چۈشىدىغىنى چۈشۈپ ۋە ئۆزى بىر ئاز قويۇقلاشقاندىن كېيىن كېسىش كېرەك. بۇنداق قىلىشتىن مەقسەت چۈشكەن سۇنى مىقدەھە ياردىمى بىلەن كۆزنىڭ تۆۋەن قىسمىغا سۈرگەندە ئۇ ئەنە شۇ جايدا توختاپ قالىدۇ. شۇنىڭ ئۈچۈن سۇنىڭ قويۇقلۇقى يېتەرلىك بولمىسا قويۇقلاشتۇرغۇچى نەرسىلەر بېرىپ قويۇقلاشتۇرۇش ياكى بەك قويۇق بولسا يۇمشاتقۇچى نەرسىلەرنى بېرىپ ئېتىدالغا چۈشۈرۈش لازىم. مەسىلەن: بېلىق گۆشى يېگۈزۈش، كوھلى جاۋاھىر سۈزمىسى تارتقۇزۇش كېرەك. چۈنكى بەك سۇيۇق ياكى قويۇق بولسا چۈشكەن سۇنى كېسىشكە بولماي قالىدۇ.
⑥ باھار پەسلىدىن باشقا پەسىللەردە كەسكىلى بولمايدۇ. كۈن ئوچۇق، شامال - بورانسىز بىر كۈنى ۋە بەك يورۇق بولمىغان بىر جاينى تاللاش كېرەك.
⑦ كېسەلنىڭ ساق كۆزىنى تېڭىپ بىر كىشىنى ئاغرىقنى تۇتۇپ بېرىشكە ياردەملەشتۈرۈش كېرەك.
⑧ بىمارنى چىداملىق بولۇش ئۈچۈن كۆندۈرۈش كېرەك.
كېسىش باسقۇچلىرى: كېسەل كۆزنىڭ ئىچكى بۇرجىكىگە ۋە بۇرنىغا قاراپ تۇرۇشقا بۇيرۇلىدۇ؛ شۇ ھالەتنى ساقلاپ تۇرغان ۋاقىت ئىچىدە مىقدەھە بىلەن تېشىلىدۇ. بۇ ئەسۋابنى ئىككى قاتلام ئارىسىغا تاكى كۆرۈش تۆشۈكىنىڭ ئۇدۇلىغا بارغۇچە كىرگۈزۈلىدۇ. ئەسۋاب شۇ يەردە بوشلۇق ۋە ئارىلىققا ئۇچرايدۇ. شۇ چاغدا بەزى ماھىر تېۋىپلار مىقدەھەنى چىقىرىۋېلىپ، شۇ ئارىلىققا مىھات[ZW(]مىھات — كۆزنى كېسىش ئەسۋابلىرىدىن بىرى.[ZW)] نىڭ قۇيرۇقىنى تىقىدۇ. ئۇنى كۆرۈش تۆشۈكىنىڭ ئۇدۇلىغىچە كىرگۈزۈپ، مىھاتنىڭ ئۆتكۈر تەرىپى ھەرىكەتلىنەلەيدىغان بوش جاي تەييارلايدۇ ۋە ئاغرىقنى چىداشقا ئۈگىتىدۇ. كېيىن مىھاتنى بەلگىلەنگەن چېگرىغىچە كىرگۈزۈپ ۋە ئۇنى سۇنىڭ ئۈستىگە قويۇپ، كۆزنى تازىلاپ، سۇ مۈڭگۈز قاتلام ئارىسىغا تۆۋەن تەرەپكە يوشۇرۇنغىچە پەسكە قاراپ بېسىلىدۇ. كېيىن مىھاتنى خېلى ۋاقىتقىچە شۇ جايدا تۇتۇپ تۇرىدۇ. شۇنداقتا نۇزۇل ئۆتكەن جايغا ئورۇنلىشىدۇ. كېيىن مىھاتنى ئۇ يەردىن ئېلىپ چۈشكەن سۇ بالدۇرقى ئورۇنغا قايتامدۇ - يوقمۇ قارىلىدۇ. ئەگەر ئۇ يەنە قايتسا، تاكى ئۇنىڭ قايتماسلىقىغا ئىشەنچ ھاسىل بولغۇچە بالدۇرقى تەدبىر تەكرارلىنىدۇ. ئەگەر سۇ پەستىكى تەرەپكە چۈشۈشكە بويسۇنمىسا، بەلكى باشقا تەرەپكە چۈشىدىغان بولسا، ئۇنى ئۆزى بېرىۋاتقان تەرەپكە ھەيدەپ، شۇ يەرگە چېچىپ تاشلىنىدۇ.
ئەگەر سۇنىڭ قايتقانلىقى )قايتىلانغانلىقى( كۆرۈلسە، دەل ئەنە شۇ ئېچىلغان تۆشۈككە مىھاتنى قايتىدىن كىرگۈزۈش لازىم. چۈنكى تۆشۈك تېخى پۈتۈپ كەتمىگەن بولىدۇ. ئەگەر تۆشۈككە قان ئېقىپ كەلسە ئۇنىمۇ چۈشۈرۈۋېلىش ۋە ئۆز ھالىغا قويماسلىق كېرەك. چۈنكى ئۇ يەردە قان قېتىپ قالسا ئۇنى داۋالىغىلى بولمايدۇ.
چۈشكەن سۇنى چۈشۈرۈۋالغاندىن كېيىن كېسەلنىڭ كۆزىگە تۇخۇم سېرىقىنى بىنەپشە سۈيى ياكى گۈل يېغى بىلەن ئارىلاشتۇرۇپ پاختا بىلەن قويۇلىدۇ. ساق كۆزىمۇ باغلاقلىق تۇرۇشى كېرەك. كېسەلنى ئۈچ كۈنگىچە قاراڭغۇ جايدا ئوڭدىسىغا ياتقۇزۇش لازىم. باش ئاغرىش ياكى كۆزدە ئىششىشلار كۆرۈلسە، بىر ھەپتىگىچە دورىنى يېڭىلاپ، تېڭىقنى ساقلاپ ئوڭدا ياتقۇزۇشقا توغرا كېلىدۇ. ئەگەر ئىششىق يۈز بەرگەندە، تېڭىق قاتتىق بولسا بىرئاز بوشىتىش كېرەك. خۇلاسە قىلىپ ئېيتقاندا، كېسەل كىشى كۆزىگە قويۇلغان نەرسىنى ئاغرىق كەتكۈچە ساقلىشى ۋە تېڭىقنى يەشمەسلىكى پايدىلىق. لېكىن تېڭىقنى ئادەتتە ئۈچ كۈندە بىر قېتىم يېشىپ، دورىنى يېڭىلاپ تۇرۇش كېرەك.
تېڭىق يېشىلگەندە قىزىلگۈلنى&nbsp; سۇدا قاينىتىپ. بىر ئاز ئىلمان ھالغا كەلگەندە پاختا ياكى لاتىنى ھۆللەپ كۆزگە قويۇش مۇمكىن.
كۆزگە چۈشكەن سۇنى كېسىۋېلىشتا تېۋىپلارنىڭ يەنە بىر قانچە ئۇسۇللىرى بار: بەزىلەر مۈڭگۈزسىمان قاتلامنى يېرىپ شۇ جايدىن سۇنى چىقىرىۋالىدۇ. بىراق بۇ ئۇسۇل خەتەرلىك. چۈنكى سۇ قويۇقراق بولسا، ئۇنىڭ بىلەن بىللە ئاقسىلسىمان سۇيۇقلۇقمۇ چىقىشى مۇمكىن.</span></div></td>
