<span id="manbilqut"> ئىرقۇننىسا - يانپاش بوغۇمىدىن باشلىنىپ پۇت ئەتراپىغا شاخلايدىغان كۆك تومۇرنىڭ ئىسمى بولۇپ، ئىرقۇننىسا كېسىلى شۇ تومۇر ئەتراپىدىكى پەي ۋە ئەسەبلەرنىڭ ئاغرىقىدۇر. ئاغرىش بەك قاتتىق بولۇپ يانپاشتىن باشلىنىپ، ئوشۇق ۋە پۇت بارماقلىرىغىچە تارقىلىدۇ.
&nbsp; سەۋەبى:&nbsp; 
1( ھەرىكەت قىلماسلىق نەتىجىسىدە بوغۇملارغا كېرەكسىز سۇيۇقلۇقلار يىغىلىدۇ.
2( ئاشقازاننىڭ ھەزىم قىلىشى ناچار بولغاندا خىلىتلار خام قېلىپ، بوغۇملارغا يىغىلىپ قالىدۇ.
3( تاماق يېيىش ۋە تۇرمۇشتىكى تەرتىپسىزلىكلەر، مەسىلەن: ھەرخىل ئىسسىق - سوغۇق تاماقلارنى قالايمىقان يېيىش، قورساق بەك توق چاغدا جىنسىي ئالاقە قىلىش، مۇنچىغا چۈشۈش، ھەرىكەت قىلىشلار.
4( زۇكام ماددىلىق بولغاندا ماددىنىڭ بوغۇملارغا تۆكۈلۈشى.
5( ئادەتتە بولۇپ تۇرىدىغان ھەيدەپ چىقىرىشنىڭ توختاپ قېلىشى، مەسىلەن: سۈرگە ئىچىپ كۆنۈپ كەتكەن كىشىلەر بىر مەزگىل سۈرگە ئىچمەي قويۇش، قان ئالدۇرۇپ ئادەتلەنگەن كىشى قان ئالدۇرماي قويۇش، ئاياللاردا ھەيز توختاپ قېلىشقا ئوخشاش سەۋەبلەر بىلەن بەدەندە ئارتۇقچە ماددا كۆپىيىپ، ئۇلارنىڭ بوغۇملارغىمۇ يىغىلىشى.
بەلغەم، قان ماددىلىرىنىڭ غەيرىي تەبىئىي ئۆزگىرىشى ياكى مەزكۇر ئىككى خىلىتنىڭ بىرلىكتە تەسىر قىلىشىدىنمۇ پەيدا بولىدۇ. يەلدىن كەمرەك يۈز بېرىدۇ، سەپرا ۋە سەۋدادىن بولمايدۇ.
باشقا بوغۇم ئاغرىقلىرى بىلەن ئوخشاپ كەتسىمۇ، لېكىن داۋالاش جەھەتتە پەرقلىنىدۇ. بۇ ئاغرىقتا قەي قىلدۇرغۇچى دورىلارنى بېرىشكە بولمايدۇ. ئۇلارنى بەرگەندە ئاغرىقنى كۈچلەندۈرۈپ قويىدۇ. چۈنكى بۇ كېسەلدە ماددا بەدەننىڭ چوڭقۇر قاتلاملىرىدا بولىدۇ.
&nbsp; داۋاسى:&nbsp; ھەيز ياكى بوۋاسىر قېنى توختاپ قېلىشى نەتىجىسىدە بولسا باسلىق ۋە ئىرقۇننىسا تومۇرىدىن قان ئېلىش كېرەك. جالىنۇس مابىز ۋە سافىن تومۇرىدىن قان ئېلىش كېرەك دېگەن. قايسى تومۇردىن قان ئېلىش مەسىلىسىنى كېسەلنىڭ ئەمەلىي ئەھۋالىغا قاراپ بەلگىلەش لازىم، قاننى كۆپ ئالماسلىق كېرەك. بۇ كېسەلنى داۋالاشتا تېزرەك چارە قىلىش لازىم. بولمىسا ئاغرىق يامانلىشىپ كېتىدۇ. ھەر قېتىم قان ئالغاندىن كېيىن بىر مەزگىل دەم ئېلىش لازىم. ئەمما ئارىلىق بەك ئۇزۇن بولماسلىقى كېرەك.
ئاغرىق توختىتىش ئۈچۈن سېرىقچېچەك بىلەن كۈنجۈت چېچىكىنى سوقۇپ قويۇش ياكى مەرزەنجۇش، بابۇنە، يانتاق ياپرىقى، ئىكلىلمىلىك ھەرقايسىسى 33 گرامدىن، سىپەندان ئۇرۇقىدىن 16 گرام سوقۇپ، بىر ئاز سۇ بىلەن نەم قىلىپ، ئاغرىق ئورۇنغا چاپلاپ قويۇلىدۇ.
داغلاش: ئىرقۇننىسا تومۇرىنى ئىزلەپ ئىرقۇننىسا تومۇرىنى ئىزدەش ئۇسۇلى ھەققىدە &gt;قان ئېلىش&lt; بابىغا قاراڭ.[ZW)] تېپىپ داغلاش بىلەن ساقايتىش مۇمكىن. داغلاش ئۇسۇلى تومۇرنى قىزىتىپ، پاچاقنىڭ ئوشۇقتىن سەككىز سانتىمېتىر يۇقىرى ئورنىدىن ئىرقۇننىسانى ئىزلەپ تومۇر ئۈستىنى داغلاش لازىم. باشقا ئۇسۇللار بىلەن نەتىجە ھاسىل بولمىغاندا داغلاش چارىسى قوللىنىلىدۇ.
بابۇنە، ئىكلىلمىلىك، قەنتەرىيون، كەرەپشە يىلتىزى، ھاشا، سېرىقچېچەك، سۆئتەر ھەرقايسىسىنى 33 گرامدىن ئېلىپ، ئۇلارنى قاينىتىپ، ئىسسىقراق ۋاقتىدا ئاغرىق تەرەپنى يۇيۇشقا بېرىلىدۇ.
ماددىنى پىشۇرۇش ئۈچۈن گۈلبىنەپشە، قەسبۇزەرىرە، ئىتئۈزۈمى، بەدىيان يىلتىزى، شاھتەررە، سۆرۈنجان ھەرقايسىسىنى يەتتە گرامدىن&nbsp; ئېلىپ، بىر دانە قۇرۇتۇلغان مۇناقى&nbsp; زۈم بىلەن بىللە قايناق سۇغا چىلاپ، تەمىنى چىقىرىپ سۈزۈپ، 40 گرام تەرەنجىبىن قوشۇپ سەھەردە بىراقلا ئىچىدۇ.
سۈرۈش ئۈچۈن گۈلبىنەپشە، كاسىنە يىلتىزى، بەدىيان يىلتىزى ھەرقايسىسى يەتتە گرامدىن، ئۇرۇقسىز ئۈزۈم توققۇز دانە، گاۋزىبان گۈلى ئۈچ گرام، ئەگرى تەرخەمەك ئۇرۇقى، قوغۇن ئۇرۇقى ھەرقايسىسى ئون گرامدىن، چۈچۈكبۇيا يىلتىزى، گاۋزىبان ياپرىقى، پىرسىياۋشان، ئىتئۈزۈمى 5.0 گرامدىن، چىلان، سەرپىستان بەش دانىدىن، ئاق لەيلى ئۇرۇقى، ھەمىشىباھار گۈلى، ئاق لەيلىگۈلى، قىزىلگۈل، سانا، ھەربىرى يەتتە گرامدىن، پۇنۇس، گۈلقەنت، خېمىرى بىنەپشە، تەرەنجىبىن، قارا شېكەر ھەربىرى 22 گرامدىن، بادام مېغىزىدىن بەش دانە ئېلىپ، پۇنۇس، تەرەنجىبىن ۋە سانادىن باشقا دورىلارنى چالا سوقۇپ، ئىككى چىنە سۇدا قاينىتىپ، سۈزۈپ، سانا، پۇنۇس، تەرەنجىبىننى تاشلاپ بىر قاينىتىپ، ئۈستىگە گۈلقەنت ۋە خېمىرى بىنەپشىنى ئارىلاشتۇرۇپ ئىستېمال قىلىنىدۇ.&nbsp; 
يېيىش ئۈچۈن ھەببى سۆرۈنجان )تەركىبى: ئايارەج پەيقىرا ۋە تۇربۇت 33 گرامدىن، شەھمەنزەل، قەنتەرىيون، سۆرۈنجان مىسرى، ماھى زەھرەج ۋە بوزىدان 17 گرامدىن، پەرپىيۇن، زەنجىۋىل، قىچا ۋە قۇندۇز قەھرىدىن يەتتە گرامدىن ئېلىپ، دورىلارنى يۇمشاق سوقۇپ، ئەلگەكتىن ئۆتكۈزۈپ، ئايارەج پەيقىرانى بىرئاز سۇدا ئېرىتىپ، تالقان دورىلارنى ئارىلاشتۇرۇپ، خېمىر قىلىپ كۇمىلاچ تەييارلىنىدۇ( دىن ھەر كۈنى ئاخشامدا 12 تالدىن ئىستېمال قىلىنىدۇ. ئىترىفىل سەغرىدىن ئۈچ گرام بېرىلىپ، ئارقىدىن ئەگرى تەرخەمەك ۋە قوغۇن ئۇرۇقى، ئوغرىتىكەنلەرنىڭ شىرىسىنى چىقىرىپ بېرىلىدۇ ياكى سۇفۇپى سۆرۈنجان Ⅰ)تەركىبى: سۆرۈنجان مىسرى 36 گرام، سانا 08.1 كىلوگرام، شەترەنجى ھىندى 29 گرام، زەپەردىن ئۈچ گرامدىن ئېلىپ، دورىلارنى يۇمشاق سوقۇپ، ئەلگەكتىن ئۆتكۈزۈپ تالقان تەييارلىنىدۇ( دىن ھەر كۈنى ئون گرامدىن، قايناق سۇ ئىستېمال قىلىنىدۇ.
