<span id="manbilqut">كۆزنىڭ مۈڭگۈز پەردە ئاستىغا چۈشكەن ئاق سۇيۇقلۇقتىن ئىبارەت بولۇپ، كۆزنىڭ كۆرۈش خىزمىتى ئوخشاش بولمىغان ھالدا بۇزۇلىدۇ. چۈشكەن ئاقنىڭ بەزىلىرى نېپىز بولۇپ &gt;بۇلۇت&lt; دەپ ئاتىلىدۇ. بەزىلىرى قېلىن بولۇپ مۇتلەق ئاق دېيىلىدۇ. ئاق بەزىدە كۆز قارىچۇقىنىڭ بىر قىسمىنى قاپلايدۇ ياكى پۈتۈنلەي قاپلايدۇ.
&nbsp; سەۋەبى:&nbsp;  كۆزنىڭ ھەرخىل زەخىملىنىشلىرى نەتىجىسىدە ئاق چۈشۈپ قالىدۇ؛ كۆز نېپىز پەردىسىنىڭ ياللۇغى ياكى كۆز ئېقىنىڭ كېسەللىرىنى داۋالاشتىكى خاتالىقلار سەۋەبچى بولىدۇ؛ شەقىقە كېسىلىدىن كېيىنمۇ كۆزگە ئاق چۈشىدۇ.
&nbsp; 
〓〓&nbsp; داۋاسى:&nbsp;  نېپىز ئاق پەردە داۋالاش بىلەن تۈزىلىپ كېتىدۇ. ئەمما قېلىن بولغىنىنىڭ ساقىيىشى تەس.
ئەگەر سەۋەب مەۋجۇت بولسا، ئالدى بىلەن قان ئېلىش ياكى سۈرگە بېرىش كېرەك. لەيلىقازاقنىڭ سۈيى، قەنتەرىيون سۈيى، ھەسەللەرنى&nbsp; مۇۋاپىق مىقداردا ئېلىپ، كۆزگە سۈركەش كېرەك. ئەگەر ئاق چوڭ قېلىن بولسا، بېرىلىدىغان دورىلار كۈچلۈك بولۇشى لازىم. مەسىلەن: كۆيدۈرۈلگەن مىس، زەڭگار، نۆشۈدۈر، نەمەكى ھىندى، كۆپۈكى دەريالەردىن مۇۋاپىق مىقداردا ئېلىپ، يۇمشاق سوقۇپ ئەلگەكتىن ئۆتكۈزۈپ، كۆزگە تارتىش ئۈچۈن بېرىلىدۇ ياكى تۆۋەندىكى نۇسخا بېرىلىدۇ.
1 - نۇسخا: قىليورغا )سۇسمار(، كۆيدۈرۈلگەن تۇخۇم شاكىلى ئون گرامدىن، قەنتەرىيون 61 گرام، مەرۋايىت، زەڭگار ئۈچ گرامدىن، بىخ مارجان، بۆرە ئەرمىنى )ياكى ئورنىغا توغرىغۇ( يەتتە مىسقالدىن، ئۆشنە ئىككى گرام، تۇتىيا سەككىز گرام، ئىپاردىن ئىككى تال ئارپا مىقدارى ئېلىپ، تاش دورىلار كۇشتە قىلىنىدۇ. باشقا دورىلارنى قوشۇپ ئۇپىدەك يۇمشاق سوقۇپ، كۈنىگە ئۈچ قېتىم سۈرمە قىلىپ كۆزگە تارتىلىدۇ. 
2 - نۇسخا: دەريا قۇلۇلىسى )سەرتان نەھرى(، كۆپۈكى دەريا، قىليورغا مايىقى، توخۇ تاشلىقى، مىسىر تۇتىياسى، تۇخۇم شاكىلى، مىس، مەرۋايىت )كۆيدۈرۈلگەن(، ھېقىق، كۆك قاشتېشى، پىلپىل، ئاقمۇچ، ئىقلىمىيا، بىخ مارجان، گىلى مەختۇم، مەھمۇدەدىن كېرەكلىك مىقداردا ئېلىپ، يۇقىرىقىدەك ئۇسۇل بىلەن تەييارلىنىدۇ.</span></div></td>
