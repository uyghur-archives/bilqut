<span id="manbilqut">&nbsp; بىھۇدە كۆپ ئويلاش، ئورۇنسىز خىيال ۋە گۇمان قىلىش، سەۋەبسىز غەم - ئەندىشە ئىچىدە ياشاپ، توغرا پىكىردىن يىراقلىشىشتەك كېسەللىك ئالامەتلىرى بىلەن ئۆتىدىغان بىر خىل روھىي كېسەللىك.
&nbsp; سەۋەبى:&nbsp;  غەيرىي تەبىئىي سەۋدا )كۆيگەن سەۋدا( بۇخاراتلىرىنىڭ مېڭىگە ئۆرلەپ چىقىرىش ياكى تەبىئىي سەۋدانىڭ نورمىدىن ئېشىپ كېتىشى ۋە بەلغەم ماددىسىنىڭ غەيرىي تەبىئىي ھالدا سەۋدا بىلەن بىرىكىشىدىن پەيدا بولىدىغان بۇخاراتلارنىڭ مېڭىگە ئۆرلەپ چىقىشىدىن كېلىپ چىققان كېسەللىك بولۇپ، سەۋدانى كۆپەيتىدىغان يېمەكلىكلەر مەسىلەن: تۇزلىغان بېلىق، پىدىگەنلەر كۆپ يېيىش، قۇرۇق مىزاجنىڭ ئېشىپ كېتىشى، ئاياللاردا ھەيزنىڭ توختاپ قېلىشى ۋە باشقىلارمۇ بۇ كېسەلگە سەۋەبچى بولۇشى مۇمكىن.
&nbsp; ئالامىتى:&nbsp;  روھىي ئالامەتلەردىن باشقا، خىلىتلارنىڭ ئۆزگىرىشىگە قاراپ تۆۋەندىكى بىر قانچە ئالامەتلەرمۇ قوشۇلۇپ كېلىدۇ. ئومۇمەن تومۇر ھەرىكىتى قاتتىق ۋە مۇختىپ بولىدۇ، سۈيدۈكمۇ مۇختە بولۇپ، قايسى خىلىت كۆپ بولسا شۇنىڭ رەڭگىگە مايىل بولىدۇ. مەسىلەن: قاندىن بولسا قىزىلغا مايىل قارا ۋە شۇنىڭغا ئوخشاش بولىدۇ.
&nbsp; داۋاسى:&nbsp;  ئالدى بىلەن مەيلى قايسى ماددىنىڭ كۆيگەنلىكى سەۋەب بولسۇن قان ئېلىش كېرەك. قان ئۇكھەل ياكى باسلىق تومۇردىن ئېلىنىدۇ. ئەگەر ئاياللاردا ھەيزنىڭ توختاپ قالغانلىقى بىلىنسە سافىن تومۇرىدىن قان ئېلىش لازىم.
ماددىنى پىشۇرغۇچى دورىلار — بىنەپشە، نېلۇپەر، گاۋزىبان ئون گرامدىن، چىلان، سەرپىستان يەتتە دانىدىن، ناۋاتتىن 33 گرام ئېلىپ، دەملەپ ئىچىشكە بېرىلىدۇ. ئەگەر ماددا مۇرەككەپ بولسا مەۋجۇت ماددىلارنىڭ پىشۇرغۇچىسى بېرىلىدۇ.
مەتبۇخى ئەفتىمۇنⅡ )تەركىبى: ئەفتىمۇن، كابىل ھېلىلىسى، ئۈستقۇددۇس 33 گرامدىن، شاھتەررە، بىستىپايەج ۋە سانا 17 گرامدىن، غارىقۇن ئۈچ گرام، سەبرىدىن يەتتە گرام ئېلىپ، ئەفتىمۇندىن باشقا دورىلارنى سوقۇپ، ئۈچ كىلوگرام سۇ قۇيۇپ، يېرىمى قالغۇچە قاينىتىپ، ئەفتىمۇننى بىر خالتىغا سېلىپ، ئاغزىنى ئىتىپ قازانغا تاشلاپ قويۇش لازىم. دورىلارنى داكا بىلەن سۈزۈپ، سىقىپ تەييارلاپ، سۈزۈلگەن دورا ئۈستىگە سەبرە ۋە غارىقۇننى &gt;سىم ئەلگەكتىن ئۆتكۈزۈلگەن&lt; سېلىپ تەييارلىنىدۇ( دىن مۇۋاپىق مىقداردا ئىستېمال قىلىپ، بەدەن تازىلىنىدۇ.
داۋائى مىشكى )تەركىبى: دۇرنەج ئەقرەبى، تېشىلمىگەن مەرۋايىت، كەھرىۋا ۋە مارجان &gt;كۆيدۈرۈلگەن&lt; 33 گرامدىن، قىرقىلغان پىلە، ئاق بەھمەن، قىزىل بەھمەن، سۇمبۇل، قاقىلە، سازەج ھىندى ۋە قەلەمپۇردىن 17 گرامدىن، ئۆشنە، زەنجىۋىل ۋە پىلپىل 13 گرامدىن، ئىپار ئۈچ گرام، ھەسەلدىن پۈتۈن دورا مىقدارىدىن ئىككى ھەسسە ئارتۇق ئېلىپ،&nbsp;  پىلە غوزىسىنى قىرقىپ، ئەلگەكتىن ئۆتكۈزۈلىدۇ، مەرۋايىتى گۇلاب بىلەن ئارىلاشتۇرۇپ ئۇندەك سوقۇلىدۇ، ئىپارنى ئازراق قەنت بىلەن ئارىلاشتۇرۇپ سوقۇلىدۇ، قالغان دورىلارنى يۇمشاق سوقۇپ، ئەلگەكتىن ئۆتكۈزۈپ، تالقان قىلىنىدۇ، ھەسەلنى ئېرىتىپ، ئۈستىگە مەرۋايىت، ئىپارنى سېلىپ ياخشى ئارىلاشتۇرۇلغاندىن كېيىن قالغان دورىلارنى سېلىپ مەجۈن تەييارلاپ، چىنە قاچىدا 40 كۈن قويۇپ تىندۇرۇپ تەييارلىنىدۇ( دىن كۈنىگە 3 ~ 5 گرامغىچە ئىستېمال قىلىنىدۇ ياكى نۇشدارى لۆئلىۋى )تەركىبى: زەرنەپ، دارچىن، ئايلىشى &gt;زورەنباد&lt;، سۇمبۇل، جويۇز، بەسباسە، قەلەمپۇر، دۇرنەج ئەقرەبى، يۇيۇلغان گىلى ئەرمىنى، گىلى مەختۇم، مەستىكى، زەپەر، كۈمۈش ۋەرەق، ئۇرۇقسىز زىرىق، ئىپار، تاباشىرنىڭ ھەرقايسىسى ئۈچ گرامدىن، قىزىلگۈل، سەئدى كوفى، گۇلابقا سۈرۈلگەن ئاق سەندەل، ئاقلانغان سېمىزئوت ئۇرۇقى &gt;تۇخۇمى خىرفە&lt; ھەرقايسىسى 33 گرامدىن، تېشىلمىگەن مەرۋايىت بەش گرام، ساپ ئامىلە 99 گرام، قىزىلگۈل گۇلابى، بېھى سۈيى، يازلىق ئالما سۈيى، تاتلىق ئامۇت سۈيى، بىدمىشكى گۇلابى، ئاق چوكا ناۋات، ھەسەل، ئاق قەنت ھەرقايسىسىدىن 396 گرامدىن ئېلىپ، ئاۋۋال ئامىلىنى سۈتكە بىر كېچە چىلاپ، ئەتىسى سۈزۈك سۇ بىلەن يۇيۇپ تازىلاپ، بىر كىلوگرام سۇ قۇيۇپ تاكى سۈيى تۈگەپ ئامىلە قالغۇچە قاينىتىپ، ئاندىن كېيىن سىم ئەلگەكتىن ئۆتكۈزۈپ، ئۈستىگە مېۋىلەر سۈيى، ناۋات، قەنت، ھەسەللەرنى سېلىپ قىيام قىلىنىدۇ. قالغان دورىلارنى يۇمشاق سوقۇپ، ئەلگەكتىن ئۆتكۈزۈپ، قىيام سوۋۇغاندىن كېيىن ئارىلاشتۇرۇپ، 40 كۈنگىچە قاچىدا ساقلاپ، تەييارلىنىدۇ( دىن ھەر كۈنى ئەتىگەن، كەچتە تاماقتىن كېيىن 7 ~ 10 گرامغىچە بېرىلىدۇ.
 سەۋدا ئالامەتلىرى ئېغىرراق بولسابۇ مېڭە ۋە يۈرەكنىڭ ھەرخىل ئاجىزلىقلىرى ئۈچۈن مۇھىم دورا نۇسخىلىرىدىن بىرى.خېمىرى ئەۋرىشىم )تەركىبى: ئۇد ھىندى، ئاق سەندەل كېپىكى بەش گرامدىن، سۇمبۇل، پىستىنىڭ تاشقى پوستى، پىشقان ئاپېلسىن شۆپىكى، مەستىكى، قەلەمپۇر، لاچىندانە ۋە سازەج ھىندى ھەرقايسىسى يەتتە گرامدىن. تېشىلمىگەن مەرۋايىت ۋە كەھرىۋا ئون گرامدىن، قىزىل ياقۇت بەش گرام، شاخ مارجان ۋە بىخ مارجان ئون گرامدىن، زەپەردىن&nbsp; ئون گرام، ئىپار، ئەنبەر بەش گرامدىن، پىلە غوزىسى &gt;قاينىتىلمىغىنى&lt; 40 گرام، گاۋزىبان ئەرقى، بىدمىشكى ئەرقى، قىزىلگۈل ئەرقى، ئالما سۈيى ۋە ئانار سۈيى 150 گرامدىن، پار سۈيى 792 گرام، قەنت 288 گرام، ھەسەل 200 گرام، ئالتۇن ۋەرەق ۋە كۈمۈش ۋەرەقتىن 40 دانىدىن ئېلىپ، ئاۋۋال پىلىنى ئەرەق ۋە سۇدا ئۈچ كۈن چىلاپ قويغاندىن كېيىن يېرىمى قالغۇدەك قاينىتىپ، ماتادىن ئۆتكۈزۈپ، تازا سىقىپ، سۈيىگە قەنت، ئالما ۋە ئانار سۇلىرىنى سېلىپ قىيام قىلىنىدۇ. مەرۋايىت، ياقۇت، مارجانلارنى كۆيدۈرۈپ بولغاندىن كېيىن كەھرىۋا بىلەن قوشۇپ سالايە قىلىنىدۇ. قالغان دورىلارنى يۇمشاق سوقۇپ، ئەلگەكتىن ئۆتكۈزۈپ، زەپەر، ئىپار، ئەنبەرلەرنى سالايە قىلىپ، گۇلاب بىلەن ئېرىتىپ، ئىلمان قىيامغا قوشۇپ، تازا ئارىلاشتۇرغاندىن كېيىن سوقۇلغان تالقانلارنى ھاۋانچىغا ئاز - ئازدىن سېلىپ، بىر ياندىن ئېزىپ، بىر ياندىن ئارىلاشتۇرۇلىدۇ ۋە ئەلگەكتىن يەنە بىر قېتىم ئۆتكۈزۈپ، قىيامغا سېلىپ ئارىلاشتۇرۇپ، ئالتۇن ۋە كۈمۈش ۋەرەقلەرنى سېلىپ ئارىلاشتۇرغاندىن كېيىن چىنە - قاچىلارغا ئېلىپ ساقلىنىدۇ( دىن كۈنىگە 1 ~ 2 قېتىم، ھەر قېتىمدا 3 ~ 7 گرامغىچە بېرىلىدۇ.</span></div></td>
