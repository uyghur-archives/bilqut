&nbsp; سەۋەبى:&nbsp;  نەم جاراھەتلەر مېڭىدىن ئوشۇقچە ھۆللۈكنىڭ بۇرۇنغا چۈشۈشى بىلەن پەيدا بولىدۇ. بۇ خىلدىكى جاراھەتلەرگە چارە كۆرۈشتە&nbsp; ئالدى بىلەن مۇۋاپىق جايغا ھىجامەت قويۇش ۋە مېڭىنى تازىلاش ئۈچۈن ھەببى قوقىيا )تەركىبى سەبرە، ئەپسەنتىن، مەستىكى ۋە غارىقۇندىن تەڭ مىقداردا، شەھمەنزەل، سوقمۇنىيا يېرىم ھەسسىدىن ئېلىپ، دورىلارنى يۇمشاق سوقۇپ، ئەلگەكتىن ئۆتكۈزۈپ، كەرەپشە ئۇرۇقىنىڭ سۈيى بىلەن خېمىر قىلىپ كۇمىلاچ تەييارلاپ، غارىقۇننى سوقماي، سىم ئەلگەككە سۈزۈپ ئۆتكۈزۈپ تەييارلىنىدۇ( دىن ئون گرامدىن ھەر كۈنىگە بىر قېتىم بېرىش كېرەك. ھەمدە تۆۋەندىكى ياغ دورا ياغلاشقا بېرىلىدۇ.
ئىسپىداج )قەلەي تۇزى(، مۇردارسەڭ، قوغۇشۇن تۇزىنى يۇمشاق سوقۇپ، گۈل يېغى بىلەن ئارىلاشتۇرۇپ، جاراھەتنى ياغلاشقا بېرىلىدۇ. 05 - نۇسخىدىكى مەلھىمى ئىسپىداج بېرىلىدۇ.
قۇرۇق جاراھەتلەرنىڭ سەۋەبى كۆيگەن خىلىتلار بولۇپ كۆپ ئۇچرايدۇ. بۇ جاراھەتلەر ئۇزۇنغا سوزۇلۇپ كەتسە، يامان سۈپەتلىك جاراھەتلەرگە ئايلىنىپ، دائىم سېسىق پۇراق چىقىرىپ تۇرىدۇ. بۇنداق بولۇشى مەزكۇر جاراھەتلەرگە ھەرخىل جاراسىملارنىڭ قوشۇلۇشىدىندۇر. بۇنىڭغا قارا خەربەقنى سوقۇپ، بۇرۇنغا پۈركۈش بىلەن بۇرۇن ئىچىنى سىركە بىلەن چايقاش كېرەك.</span></div></td>
